/**
 * AlloFlow — Local App Server (B4.4)
 *
 * HTTP server that serves local-app/build/ for the local-only AlloFlow app.
 * Mirrors webAppServer.js but injects __alloLocalConfig (not ai_config.json).
 *
 * Config source: ~/.alloflow/local_config.json
 * Inject target: window.__alloLocalConfig in every served index.html
 *
 * Used by admin/public/main.js to serve the local app alongside the web app.
 */

'use strict';

const http = require('http');
const fs   = require('fs');
const path = require('path');
const os   = require('os');

let _server     = null;
let _serverPort = 3730;

const LOCAL_CONFIG_FILE  = path.join(os.homedir(), '.alloflow', 'local_config.json');
const AI_CONFIG_FILE     = path.join(os.homedir(), '.alloflow', 'ai_config.json');
const GEMINI_TOKEN_FILE  = path.join(os.homedir(), '.alloflow', 'gemini_token.json');
const COPILOT_TOKEN_FILE = path.join(os.homedir(), '.alloflow', 'copilot_token.json');

const MIME_TYPES = {
    '.html': 'text/html; charset=utf-8',
    '.css':  'text/css; charset=utf-8',
    '.js':   'application/javascript; charset=utf-8',
    '.json': 'application/json; charset=utf-8',
    '.png':  'image/png',
    '.jpg':  'image/jpeg',
    '.jpeg': 'image/jpeg',
    '.gif':  'image/gif',
    '.svg':  'image/svg+xml',
    '.ico':  'image/x-icon',
    '.woff': 'font/woff',
    '.woff2':'font/woff2',
    '.ttf':  'font/ttf',
    '.map':  'application/json',
    '.wav':  'audio/wav',
    '.mp3':  'audio/mpeg',
    '.ogg':  'audio/ogg',
    '.wasm': 'application/wasm',
};

const isStaticExt = new Set([
    '.js', '.css', '.png', '.jpg', '.jpeg', '.gif', '.svg',
    '.ico', '.woff', '.woff2', '.ttf', '.map', '.wav', '.mp3',
    '.ogg', '.wasm',
]);

// ── Config helpers ────────────────────────────────────────────────────────────

/**
 * Build the __alloLocalConfig injection script.
 * Reads local_config.json and injects it into the HTML before the app loads.
 * NOTE: Tokens are NOT included here for security. Use server-side proxy endpoints instead.
 */
function buildLocalConfigScript() {
    const DEFAULT = {
        llmEngineUrl: 'http://localhost:1234',
        sqliteUrl:    'http://localhost:3747',
        piperEnabled: true,
        defaultModel: 'default',
    };

    let cfg = DEFAULT;
    try {
        if (fs.existsSync(LOCAL_CONFIG_FILE)) {
            const raw = fs.readFileSync(LOCAL_CONFIG_FILE, 'utf-8');
            cfg = { ...DEFAULT, ...JSON.parse(raw) };
        }
    } catch (err) {
        console.warn('[localApp] Failed to read local_config.json:', err.message);
    }

    // Merge AI config (aiProvider, imageProvider, OAuth state) into local config
    try {
        if (fs.existsSync(AI_CONFIG_FILE)) {
            const ai = JSON.parse(fs.readFileSync(AI_CONFIG_FILE, 'utf-8'));
            if (ai.aiProvider) cfg.aiProvider = ai.aiProvider;
            if (ai.imageProvider) cfg.imageProvider = ai.imageProvider;
            if (ai.copilot?.endpoint) cfg.copilotEndpoint = ai.copilot.endpoint;
        }
    } catch (_) {}

    // For cloud providers (gemini, copilot), redirect llmEngineUrl to this server
    // so that ai_local_module.js calls /v1/chat/completions here instead of LM Studio.
    const CLOUD_PROVIDERS = new Set(['gemini', 'copilot', 'openai']);
    if (CLOUD_PROVIDERS.has(cfg.aiProvider)) {
        cfg.llmEngineUrl = `http://localhost:${_serverPort}`;
    }

    // SECURITY: Do NOT inject tokens here. Server uses tokens internally via API proxy.
    // Client calls: POST /v1/chat/completions (OpenAI-compat) — dispatched by provider.
    // Server authenticates with stored tokens and returns only the AI response.

    return `<script>
/* AlloFlow Local Config Injection — generated by localAppServer.js */
window.__alloLocalConfig = ${JSON.stringify(cfg, null, 2)};
window.__alloLocalDBUrl  = window.__alloLocalConfig.sqliteUrl;
</script>`;
}

// ── API Proxy Endpoints ───────────────────────────────────────────────────────

/**
 * Load and validate Gemini access token from disk.
 * @returns {string|null} Valid access token or null if missing/expired
 */
function loadGeminiToken() {
    try {
        if (!fs.existsSync(GEMINI_TOKEN_FILE)) return null;
        const tok = JSON.parse(fs.readFileSync(GEMINI_TOKEN_FILE, 'utf-8'));
        if (!tok?.access_token) return null;
        if (tok.expiry && Date.now() >= tok.expiry - 60 * 1000) return null; // Expired
        return tok.access_token;
    } catch {
        return null;
    }
}

/**
 * Load and validate Copilot access token from disk.
 * @returns {string|null} Valid access token or null if missing/expired
 */
function loadCopilotToken() {
    try {
        if (!fs.existsSync(COPILOT_TOKEN_FILE)) return null;
        const tok = JSON.parse(fs.readFileSync(COPILOT_TOKEN_FILE, 'utf-8'));
        if (!tok?.access_token) return null;
        if (tok.expiry && Date.now() >= tok.expiry - 60 * 1000) return null;
        return tok.access_token;
    } catch {
        return null;
    }
}

/**
 * Handle /api/ai/chat proxy — forward requests to Gemini with server-side token.
 * @param {http.ServerRequest} req
 * @param {http.ServerResponse} res
 * @param {object} body Parsed JSON body
 */
async function handleAiChatApi(req, res, body) {
    try {
        const { provider, messages, model } = body;
        
        if (!provider || provider === 'gemini') {
            const token = loadGeminiToken();
            if (!token) {
                res.writeHead(401, { 'Content-Type': 'application/json' });
                return res.end(JSON.stringify({ error: 'Gemini token not available' }));
            }

            // Call Gemini OpenAI-compatible API with server-side token
            const geminiRes = await fetch('https://generativelanguage.googleapis.com/v1beta/openai/chat/completions', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${token}`,
                },
                body: JSON.stringify({ messages, model: model || 'gemini-2.0-flash' }),
            });

            const data = await geminiRes.json();
            res.writeHead(geminiRes.status, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify(data));
        } else if (provider === 'copilot') {
            const token = loadCopilotToken();
            if (!token) {
                res.writeHead(401, { 'Content-Type': 'application/json' });
                return res.end(JSON.stringify({ error: 'Copilot token not available' }));
            }

            // Call Copilot API with server-side token
            const copilotRes = await fetch('https://api.copilot.microsoft.com/chat', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${token}`,
                },
                body: JSON.stringify({ messages }),
            });

            const data = await copilotRes.json();
            res.writeHead(copilotRes.status, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify(data));
        } else {
            res.writeHead(400, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify({ error: 'Unknown AI provider' }));
        }
    } catch (err) {
        console.error('[localApp:api] Error in chat proxy:', err.message);
        res.writeHead(500, { 'Content-Type': 'application/json' });
        return res.end(JSON.stringify({ error: 'Internal server error' }));
    }
}

/**
 * Read the configured aiProvider from ai_config.json (at request time).
 * @returns {string} aiProvider — 'gemini', 'copilot', 'lmstudio', etc.
 */
function readAiProvider() {
    try {
        if (fs.existsSync(AI_CONFIG_FILE)) {
            const ai = JSON.parse(fs.readFileSync(AI_CONFIG_FILE, 'utf-8'));
            return ai.aiProvider || 'lmstudio';
        }
    } catch {}
    return 'lmstudio';
}

/**
 * Log an AI proxy event to the console.
 * @param {string} route    e.g. '/v1/chat/completions'
 * @param {string} provider e.g. 'gemini'
 * @param {number} status   HTTP status returned
 * @param {number} ms       Elapsed milliseconds
 */
function logAIProxy(route, provider, status, ms) {
    console.log(`[localApp:ai-proxy] ${route} → ${provider} | ${status} | ${ms}ms`);
}

/**
 * Handle /v1/chat/completions — OpenAI-compatible proxy dispatched by aiProvider.
 * Gemini and Copilot are handled server-side with stored tokens.
 * LM Studio requests are passed through to localhost:1234.
 */
async function handleV1ChatCompletions(req, res, body) {
    const t0 = Date.now();
    const provider = readAiProvider();

    try {
        if (provider === 'gemini') {
            const token = loadGeminiToken();
            if (!token) {
                logAIProxy('/v1/chat/completions', 'gemini', 401, Date.now() - t0);
                res.writeHead(401, { 'Content-Type': 'application/json' });
                return res.end(JSON.stringify({ error: 'Gemini token not available — re-authorize in Settings' }));
            }
            // Forward as-is to Gemini's OpenAI-compat endpoint
            const upstream = await fetch('https://generativelanguage.googleapis.com/v1beta/openai/chat/completions', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${token}` },
                body: JSON.stringify(body),
            });
            const data = await upstream.json();
            logAIProxy('/v1/chat/completions', 'gemini', upstream.status, Date.now() - t0);
            res.writeHead(upstream.status, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify(data));

        } else if (provider === 'copilot') {
            const token = loadCopilotToken();
            if (!token) {
                logAIProxy('/v1/chat/completions', 'copilot', 401, Date.now() - t0);
                res.writeHead(401, { 'Content-Type': 'application/json' });
                return res.end(JSON.stringify({ error: 'Copilot token not available — re-authorize in Settings' }));
            }
            // GitHub Copilot uses the standard OpenAI completions endpoint
            const copilotEndpoint = (() => {
                try {
                    if (fs.existsSync(AI_CONFIG_FILE)) {
                        const ai = JSON.parse(fs.readFileSync(AI_CONFIG_FILE, 'utf-8'));
                        return ai.copilot?.endpoint || 'https://api.githubcopilot.com';
                    }
                } catch {}
                return 'https://api.githubcopilot.com';
            })();
            const upstream = await fetch(`${copilotEndpoint}/chat/completions`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${token}`,
                    'Editor-Version': 'AlloFlow/1.0',
                    'Copilot-Integration-Id': 'vscode-chat',
                },
                body: JSON.stringify(body),
            });
            const data = await upstream.json();
            logAIProxy('/v1/chat/completions', 'copilot', upstream.status, Date.now() - t0);
            res.writeHead(upstream.status, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify(data));

        } else {
            // LM Studio passthrough
            const lmUrl = 'http://localhost:1234/v1/chat/completions';
            const upstream = await fetch(lmUrl, {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(body),
            });
            const data = await upstream.json();
            logAIProxy('/v1/chat/completions', provider, upstream.status, Date.now() - t0);
            res.writeHead(upstream.status, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify(data));
        }
    } catch (err) {
        console.error('[localApp:ai-proxy] /v1/chat/completions error:', err.message);
        logAIProxy('/v1/chat/completions', provider, 500, Date.now() - t0);
        res.writeHead(500, { 'Content-Type': 'application/json' });
        return res.end(JSON.stringify({ error: 'AI proxy error: ' + err.message }));
    }
}

/**
 * Handle /v1/models — returns a virtual model list for cloud providers,
 * or passes through to LM Studio for local providers.
 * Used by the app's offline banner check.
 */
async function handleV1Models(req, res) {
    const t0 = Date.now();
    const provider = readAiProvider();

    if (provider === 'gemini') {
        const data = { object: 'list', data: [{ id: 'gemini-2.0-flash', object: 'model', owned_by: 'google' }] };
        logAIProxy('/v1/models', 'gemini', 200, Date.now() - t0);
        res.writeHead(200, { 'Content-Type': 'application/json' });
        return res.end(JSON.stringify(data));
    } else if (provider === 'copilot') {
        const data = { object: 'list', data: [{ id: 'gpt-4o', object: 'model', owned_by: 'github' }] };
        logAIProxy('/v1/models', 'copilot', 200, Date.now() - t0);
        res.writeHead(200, { 'Content-Type': 'application/json' });
        return res.end(JSON.stringify(data));
    } else {
        // LM Studio passthrough
        try {
            const upstream = await fetch('http://localhost:1234/v1/models');
            const data = await upstream.json();
            logAIProxy('/v1/models', provider, upstream.status, Date.now() - t0);
            res.writeHead(upstream.status, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify(data));
        } catch (err) {
            logAIProxy('/v1/models', provider, 503, Date.now() - t0);
            res.writeHead(503, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify({ error: 'LM Studio not reachable' }));
        }
    }
}

/**
 * Handle /api/gemini/proxy/:model — accepts Gemini-native format and proxies to Gemini API.
 * Used by callGemini() in local app when aiProvider=gemini so the client never needs a token.
 * @param {http.ServerRequest} req
 * @param {http.ServerResponse} res
 * @param {string} model  Gemini model name (e.g. 'gemini-2.0-flash')
 * @param {object} body   Gemini-native payload {contents, generationConfig?, tools?, ...}
 */
async function handleGeminiNativeProxy(req, res, model, body) {
    const t0 = Date.now();
    const token = loadGeminiToken();
    if (!token) {
        logAIProxy(`/api/gemini/proxy/${model}`, 'gemini', 401, Date.now() - t0);
        res.writeHead(401, { 'Content-Type': 'application/json' });
        return res.end(JSON.stringify({ error: 'Gemini token not available — re-authorize in Settings' }));
    }

    const safeModel = model || 'gemini-2.0-flash';
    const url = `https://generativelanguage.googleapis.com/v1beta/models/${safeModel}:generateContent`;
    try {
        const upstream = await fetch(url, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${token}` },
            body: JSON.stringify(body),
        });
        const data = await upstream.json();
        logAIProxy(`/api/gemini/proxy/${safeModel}`, 'gemini', upstream.status, Date.now() - t0);
        res.writeHead(upstream.status, { 'Content-Type': 'application/json' });
        return res.end(JSON.stringify(data));
    } catch (err) {
        console.error('[localApp:ai-proxy] /api/gemini/proxy error:', err.message);
        logAIProxy(`/api/gemini/proxy/${safeModel}`, 'gemini', 500, Date.now() - t0);
        res.writeHead(500, { 'Content-Type': 'application/json' });
        return res.end(JSON.stringify({ error: 'Gemini proxy error: ' + err.message }));
    }
}

// ── Path resolution ───────────────────────────────────────────────────────────

/**
 * @param {boolean} isPackaged
 */
function resolveLocalAppDir(isPackaged) {
    if (isPackaged) {
        return path.join(process.resourcesPath, 'local-app');
    }
    // Dev mode — local-app/build/ relative to admin/public/
    return path.join(__dirname, '..', '..', 'local-app', 'build');
}

// ── Server ────────────────────────────────────────────────────────────────────

/**
 * Start the local app HTTP server.
 * @param {number}  port       Port to listen on (default 3730)
 * @param {boolean} isPackaged Electron packaging flag
 * @returns {Promise<number>}  Actual port the server is listening on
 */
function startLocalAppServer(port, isPackaged) {
    _serverPort = port || 3730;
    const appDir     = resolveLocalAppDir(isPackaged);
    const publicDir  = path.join(__dirname, '..', '..', 'local-app', 'public');

    if (!fs.existsSync(appDir)) {
        // Build dir doesn't exist yet — warn but don't fail.
        // Admin can trigger a build or it may be generated later.
        console.warn('[localApp] Build directory not found:', appDir);
        console.warn('[localApp] Run: node local_build.js  to build the local app');
    }

    const indexPath = path.join(appDir, 'index.html');

    return new Promise((resolve, reject) => {
        _server = http.createServer((req, res) => {
            res.setHeader('Access-Control-Allow-Origin',  '*');
            res.setHeader('Access-Control-Allow-Methods', 'GET, POST, OPTIONS');
            res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

            if (req.method === 'OPTIONS') {
                res.writeHead(204);
                return res.end();
            }

            let urlPath = (req.url || '/').split('?')[0];

            // ── API Proxy Routes (before static file serving) ─────────────────
            if (urlPath === '/api/ai/chat' && req.method === 'POST') {
                let body = '';
                req.on('data', chunk => { body += chunk.toString(); });
                req.on('end', async () => {
                    try {
                        const parsed = JSON.parse(body);
                        await handleAiChatApi(req, res, parsed);
                    } catch (err) {
                        res.writeHead(400, { 'Content-Type': 'application/json' });
                        res.end(JSON.stringify({ error: 'Invalid JSON' }));
                    }
                });
                return;
            }

            // OpenAI-compatible proxy — dispatches to Gemini/Copilot/LM Studio
            if (urlPath === '/v1/chat/completions' && req.method === 'POST') {
                let body = '';
                req.on('data', chunk => { body += chunk.toString(); });
                req.on('end', async () => {
                    try {
                        const parsed = JSON.parse(body);
                        await handleV1ChatCompletions(req, res, parsed);
                    } catch (err) {
                        res.writeHead(400, { 'Content-Type': 'application/json' });
                        res.end(JSON.stringify({ error: 'Invalid JSON' }));
                    }
                });
                return;
            }

            if (urlPath === '/v1/models' && (req.method === 'GET' || req.method === 'OPTIONS')) {
                handleV1Models(req, res).catch(err => {
                    console.error('[localApp:ai-proxy] /v1/models error:', err.message);
                    res.writeHead(500, { 'Content-Type': 'application/json' });
                    res.end(JSON.stringify({ error: err.message }));
                });
                return;
            }

            // Gemini native-format proxy — used by callGemini() when aiProvider=gemini
            if (urlPath.startsWith('/api/gemini/proxy/') && req.method === 'POST') {
                const model = decodeURIComponent(urlPath.slice('/api/gemini/proxy/'.length));
                let body = '';
                req.on('data', chunk => { body += chunk.toString(); });
                req.on('end', async () => {
                    try {
                        const parsed = JSON.parse(body);
                        await handleGeminiNativeProxy(req, res, model, parsed);
                    } catch (err) {
                        res.writeHead(400, { 'Content-Type': 'application/json' });
                        res.end(JSON.stringify({ error: 'Invalid JSON' }));
                    }
                });
                return;
            }

            // ── Static file serving ───────────────────────────────────────────
            if (urlPath === '/') urlPath = '/index.html';

            // Prevent path traversal
            const safePath = path.normalize(urlPath).replace(/^(\.\.(\/|\\|$))+/, '');
            const filePath  = path.join(appDir, safePath);

            if (!filePath.startsWith(appDir)) {
                res.writeHead(403);
                return res.end('Forbidden');
            }

            const ext     = path.extname(filePath).toLowerCase();
            const isStatic = isStaticExt.has(ext);

            // ── Serve index.html with injected config ──────────────────────
            if (urlPath === '/index.html' || (!fs.existsSync(filePath) && !isStatic)) {
                // Check both build dir and public/ for index.html
                const htmlSource = fs.existsSync(indexPath) ? indexPath
                    : path.join(publicDir, 'index.html');

                if (!fs.existsSync(htmlSource)) {
                    res.writeHead(503, { 'Content-Type': 'text/html; charset=utf-8' });
                    return res.end(buildNotReadyPage());
                }

                try {
                    let html = fs.readFileSync(htmlSource, 'utf-8');
                    // Inject local config before first </head> or at start of <head>
                    const inject = buildLocalConfigScript();
                    if (html.includes('<head>')) {
                        html = html.replace('<head>', '<head>\n  ' + inject);
                    } else {
                        html = inject + '\n' + html;
                    }
                    res.writeHead(200, {
                        'Content-Type':  'text/html; charset=utf-8',
                        'Cache-Control': 'no-store, no-cache, must-revalidate',
                    });
                    return res.end(html);
                } catch (err) {
                    console.error('[localApp] Error serving index.html:', err.message);
                    res.writeHead(500);
                    return res.end('Internal Server Error');
                }
            }

            // ── Static assets: 404 if missing (no HTML fallback) ──────────
            if (isStatic && !fs.existsSync(filePath)) {
                // Try falling back to public/ for un-built assets (dev mode)
                const pubFallback = path.join(publicDir, safePath);
                if (fs.existsSync(pubFallback)) {
                    const content = fs.readFileSync(pubFallback);
                    res.writeHead(200, {
                        'Content-Type':  MIME_TYPES[ext] || 'application/octet-stream',
                        'Cache-Control': 'no-store, no-cache, must-revalidate',
                    });
                    return res.end(content);
                }
                res.writeHead(404);
                return res.end('Not Found');
            }

            // ── Serve static file ─────────────────────────────────────────
            try {
                const content    = fs.readFileSync(filePath);
                const contentType = MIME_TYPES[ext] || 'application/octet-stream';
                res.writeHead(200, {
                    'Content-Type':  contentType,
                    'Cache-Control': 'no-store, no-cache, must-revalidate',
                });
                return res.end(content);
            } catch (err) {
                res.writeHead(404);
                return res.end('Not Found');
            }
        });

        _server.on('error', (err) => {
            if (err.code === 'EADDRINUSE') {
                console.warn(`[localApp] Port ${_serverPort} in use, trying ${_serverPort + 1}`);
                _serverPort++;
                _server.listen(_serverPort, '127.0.0.1');
            } else {
                reject(err);
            }
        });

        _server.listen(_serverPort, '127.0.0.1', () => {
            console.log(`[localApp] Local app serving at http://localhost:${_serverPort}`);
            resolve(_serverPort);
        });
    });
}

/**
 * Stop the local app server.
 */
function stopLocalAppServer() {
    if (_server) {
        try { _server.close(); } catch {}
        _server = null;
        console.log('[localApp] Local app server stopped');
    }
}

/** Returns the current local app port. */
function getLocalAppPort() {
    return _serverPort;
}

