#!/usr/bin/env node
/**
 * Build visual_panel_module.js from visual_panel_source.jsx
 *
 * Usage:
 *   node _build_visual_panel_module.js              # write to visual_panel_module.js
 *   node _build_visual_panel_module.js --check      # write to .candidate.js and diff
 *   node _build_visual_panel_module.js --dry-run    # alias for --check
 *
 * Mirrors _build_anchor_charts_module.js and _build_concept_pictionary_module.js.
 * Compiles JSX → React.createElement via esbuild, wraps in IIFE with a11y CSS,
 * duplicate-load guard, React aliases, and a window-resolving sanitizeHtml.
 *
 * The module exposes:
 *   window.AlloModules.VisualPanelGrid
 *   window.AlloModules.VisualPanelModule = true
 *
 * Loaded from CDN per AlloFlowANTI.txt loadModule('VisualPanelModule', ...).
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'visual_panel_source.jsx');
const OUTPUT = path.join(ROOT, 'visual_panel_module.js');
const DEPLOY_OUT = path.join(ROOT, 'desktop/web-app', 'public', 'visual_panel_module.js');
const TMP = path.join(ROOT, '_tmp_visual_panel_entry.jsx');

const args = process.argv.slice(2);
const checkMode = args.includes('--check') || args.includes('--dry-run');

if (!fs.existsSync(SOURCE)) {
    console.error('[VisualPanel] Source not found:', SOURCE);
    process.exit(1);
}

const source = fs.readFileSync(SOURCE, 'utf-8');

// The source.jsx defines its own inline sanitizeHtml at top. The module wraps
// that function in a window-resolving variant (so the CDN-loaded module picks
// up the monolith's sanitizeHtml when available, falls back to inline).
// We strip the source-level definition and inject the wrapper inside the IIFE.
const SANITIZE_HTML_RE = /\/\/ HTML sanitizer[\s\S]*?function sanitizeHtml\(html\) \{[\s\S]*?\n\}/;
let strippedSource = source.replace(SANITIZE_HTML_RE, '// sanitizeHtml provided by IIFE wrapper');

if (strippedSource === source) {
    console.warn('[VisualPanel] Warning: did not find sanitizeHtml block to strip — source may have changed shape');
}

const entry = `
/* global React, useState, useEffect, useRef, useCallback, useMemo */

${strippedSource}

window.__visualPanelExports = { VisualPanelGrid };
`;

fs.writeFileSync(TMP, entry, 'utf-8');

console.log('[VisualPanel] Compiling visual_panel_source.jsx with esbuild...');
try {
    execSync(
        `npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`,
        { cwd: ROOT, stdio: 'inherit' }
    );
} catch (e) {
    console.error('[VisualPanel] esbuild compilation failed');
    try { fs.unlinkSync(TMP); } catch (_) {}
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    .replace(/\/\*.*global.*\*\/\n/g, '')
    .replace(/window\.__visualPanelExports\s*=\s*\{[^}]+\};?\s*/, '')
    .trim();

try { fs.unlinkSync(TMP); } catch (_) {}
try { fs.unlinkSync(TMP + '.compiled.js'); } catch (_) {}

const outputCode = `/**
 * AlloFlow Visual Panel Module
 *
 * Contains: VisualPanelGrid
 *
 * Auto-generated by _build_visual_panel_module.js — DO NOT EDIT MANUALLY.
 * Source: visual_panel_source.jsx
 */
(function() {
  'use strict';
  // WCAG 2.2 AA: Accessibility CSS
  if (!document.getElementById("visual-panel-a11y")) { var _s = document.createElement("style"); _s.id = "visual-panel-a11y"; _s.textContent = "@media (prefers-reduced-motion: reduce) { *, *::before, *::after { animation-duration: 0.01ms !important; animation-iteration-count: 1 !important; transition-duration: 0.01ms !important; } } .text-slate-600 { color: #64748b !important; }"; document.head.appendChild(_s); }

  // ── Duplicate-load guard ──
  if (window.__visualPanelModuleLoaded) {
    console.warn('[VisualPanelModule] Already loaded — skipping');
    return;
  }
  window.__visualPanelModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[VisualPanelModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;

  // sanitizeHtml is defined in the monolith (AlloFlowANTI.txt) and exposed on window.
  // The CDN module IIFE can't see top-level monolith consts. We use a wrapper that
  // resolves window.sanitizeHtml at CALL time (not module-load time), so it works
  // even if the module loads before the monolith populates window.sanitizeHtml.
  // Falls back to a minimal inline sanitizer matching the monolith's behavior.
  function sanitizeHtml(html) {
    var fn = (typeof window !== 'undefined' && window.sanitizeHtml);
    if (typeof fn === 'function' && fn !== sanitizeHtml) return fn(html);
    // Inline fallback — matches AlloFlowANTI.txt sanitizeHtml output
    if (!html || typeof html !== 'string') return '';
    var clean = html.replace(/<script[\\s\\S]*?<\\/script>/gi, '');
    clean = clean.replace(/\\s+on\\w+\\s*=\\s*["'][^"']*["']/gi, '');
    clean = clean.replace(/\\s+on\\w+\\s*=\\s*\\S+/gi, '');
    clean = clean.replace(/<\\/?(iframe|object|embed|form|link|meta|base)[^>]*>/gi, '');
    clean = clean.replace(/href\\s*=\\s*["']?javascript:/gi, 'href="');
    clean = clean.replace(/src\\s*=\\s*["']?javascript:/gi, 'src="');
    return clean;
  }

  // ═══════════════════════════════════════════════════════════════
  // VISUAL PANEL COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

${compiled}


  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.VisualPanelGrid = VisualPanelGrid;
  window.AlloModules.VisualPanelModule = true;

  console.log('[VisualPanelModule] VisualPanelGrid registered successfully');
})();
`;

if (checkMode) {
    const candidatePath = OUTPUT + '.candidate';
    fs.writeFileSync(candidatePath, outputCode, 'utf-8');
    console.log(`[VisualPanel] Check mode: wrote candidate to ${candidatePath}`);
    console.log(`[VisualPanel] Diff candidate against current module to inspect drift.`);
    process.exit(0);
}

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
try {
    if (!fs.existsSync(path.dirname(DEPLOY_OUT))) {
        fs.mkdirSync(path.dirname(DEPLOY_OUT), { recursive: true });
    }
    fs.writeFileSync(DEPLOY_OUT, outputCode, 'utf-8');
} catch (e) {
    console.warn('[VisualPanel] Could not sync to desktop/web-app/public/:', e.message);
}
const lineCount = outputCode.split('\n').length;
console.log(`[VisualPanel] Built ${OUTPUT} (${lineCount} lines)`);
console.log(`[VisualPanel] Synced to ${DEPLOY_OUT}`);
