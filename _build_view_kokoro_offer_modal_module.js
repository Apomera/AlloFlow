#!/usr/bin/env node
/**
 * Build view_kokoro_offer_modal_module.js from view_kokoro_offer_modal_source.jsx
 *
 * Usage: node _build_view_kokoro_offer_modal_module.js
 *
 * Compiles JSX → React.createElement via esbuild, wraps in IIFE,
 * registers on window.AlloModules.KokoroOfferModal.
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'view_kokoro_offer_modal_source.jsx');
const OUTPUT = path.join(ROOT, 'view_kokoro_offer_modal_module.js');
const TMP = path.join(ROOT, '_tmp_kokoro_entry.jsx');

if (!fs.existsSync(SOURCE)) {
    console.error('Source not found:', SOURCE);
    process.exit(1);
}

const source = fs.readFileSync(SOURCE, 'utf-8');

const entry = `
/* global React */

${source}

window.__kokoroExports = { KokoroOfferModal };
`;

fs.writeFileSync(TMP, entry, 'utf-8');

console.log('Compiling view_kokoro_offer_modal_source.jsx with esbuild...');
try {
    execSync(`npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`, {
        cwd: ROOT,
        stdio: 'inherit'
    });
} catch (e) {
    console.error('esbuild compilation failed');
    try { fs.unlinkSync(TMP); } catch(_) {}
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    .replace(/\/\*.*global.*\*\/\n/g, '')
    .replace(/window\.__kokoroExports\s*=\s*\{[^}]+\};?\s*/, '')
    .trim();

fs.unlinkSync(TMP);
fs.unlinkSync(TMP + '.compiled.js');

const outputCode = `/**
 * AlloFlow Kokoro Offer Modal Module
 *
 * Modal offering Kokoro TTS download when cloud voice is unavailable.
 *
 * Auto-generated by _build_view_kokoro_offer_modal_module.js — DO NOT EDIT MANUALLY.
 * Source: view_kokoro_offer_modal_source.jsx
 */
(function() {
  'use strict';

  if (window.AlloModules && window.AlloModules.KokoroOfferModal) {
    console.log('[CDN] KokoroOfferModal already loaded, skipping');
    return;
  }

  var React = window.React;
  if (!React) { console.error('[KokoroOfferModal] React not found on window'); return; }

${compiled}

  window.AlloModules = window.AlloModules || {};
  window.AlloModules.KokoroOfferModal = { KokoroOfferModal: KokoroOfferModal };

  console.log('[CDN] KokoroOfferModal loaded');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
const lineCount = outputCode.split('\n').length;
console.log(`Built ${OUTPUT} (${lineCount} lines)`);
