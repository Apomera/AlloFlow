#!/usr/bin/env node
/**
 * Build script: Adventure UI components → adventure_module.js
 *
 * Contains: MissionReportCard, ClimaxProgressBar, AdventureAmbience,
 *           InventoryGrid, DiceOverlay, AdventureShop, CastLobby,
 *           playAdventureEventSound, playGenerativeSoundscape,
 *           playDiceSound, getD20Rotation
 *
 * Reads JSX source, transforms via esbuild, wraps in IIFE with
 * dependency injection, outputs CDN-ready module.
 */

const fs = require('fs');
const { execSync } = require('child_process');
const path = require('path');

const SOURCE_JSX = path.join(__dirname, 'adventure_source.jsx');
const OUTPUT = path.join(__dirname, 'adventure_module.js');
const OUTPUT_PUBLIC = path.join(__dirname, 'prismflow-deploy', 'public', 'adventure_module.js');
const TEMP = path.join(__dirname, '_temp_adventure.jsx');

console.log('📦 Building adventure_module.js...\n');

if (!fs.existsSync(SOURCE_JSX)) {
    console.error('❌ adventure_source.jsx not found');
    process.exit(1);
}

const jsxSource = fs.readFileSync(SOURCE_JSX, 'utf-8');
console.log(`   Read ${jsxSource.split('\n').length} lines from adventure_source.jsx`);

// ── Write temp JSX file for esbuild ──
const preamble = `/* eslint-disable */
// Auto-extracted Adventure UI components — DO NOT EDIT MANUALLY
// Generated by _build_adventure_module.js
`;

fs.writeFileSync(TEMP, preamble + jsxSource, 'utf-8');
console.log(`   Temp JSX file: ${TEMP}`);

// ── Transform JSX via esbuild ──
console.log('   Running esbuild JSX transform...');
let transformed;
try {
    transformed = execSync(
        `npx esbuild "${TEMP}" --jsx=transform --bundle=false --target=es2020`,
        { encoding: 'utf-8', maxBuffer: 1024 * 1024 * 50 }
    );
} catch (e) {
    console.error('❌ esbuild failed:', e.message);
    if (e.stderr) console.error(e.stderr);
    process.exit(1);
}

// Clean up temp file
fs.unlinkSync(TEMP);
console.log('   ✅ JSX transformed successfully');

// ── Post-process: fix const → var for top-level declarations ──
const componentNames = [
    'MissionReportCard', 'ClimaxProgressBar', 'AdventureAmbience',
    'InventoryGrid', 'DiceOverlay', 'AdventureShop', 'CastLobby'
];
const utilNames = [
    'playAdventureEventSound', 'playGenerativeSoundscape',
    'playDiceSound', 'getD20Rotation'
];
[...componentNames, ...utilNames].forEach(name => {
    const pattern = new RegExp(`const ${name} = `, 'g');
    transformed = transformed.replace(pattern, `var ${name} = `);
});

// ── Icons used by adventure components ──
const iconList = [
    'Backpack', 'BookOpen', 'Key', 'MapIcon', 'RefreshCw',
    'ShoppingBag', 'Trophy', 'X', 'Zap'
];

// ── Build IIFE wrapper ──
const iifeHeader = `/**
 * AlloFlow Adventure UI Module
 *
 * Contains: ${componentNames.join(', ')}
 * Utilities: ${utilNames.join(', ')}
 *
 * Auto-generated by _build_adventure_module.js — DO NOT EDIT MANUALLY.
 * Source: adventure_source.jsx
 */
(function() {
  'use strict';

  // ── Duplicate-load guard ──
  if (window.__adventureModuleLoaded) {
    console.warn('[AdventureModule] Already loaded — skipping');
    return;
  }
  window.__adventureModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[AdventureModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  // ── App dependencies from window ──
  var LanguageContext = window.AlloLanguageContext;
  var _shared = window.__alloShared || {};
  var warnLog = _shared.warnLog || function() { console.warn.apply(console, arguments); };
  var debugLog = window.__alloDebugLog || function() {};
  var getGlobalAudioContext = window.getGlobalAudioContext || function() { return null; };
  var isGlobalMuted = window.__alloIsGlobalMuted || function() { return false; };

  // ── Lucide icons from host app ──
  var _icons = window.AlloIcons || {};
${iconList.map(ic => `  var ${ic} = _icons.${ic} || function() { return null; };`).join('\n')}

  // ═══════════════════════════════════════════════════════════════
  // ADVENTURE COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

`;

const iifeFooter = `

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
${componentNames.map(n => `  window.AlloModules.${n} = ${n};`).join('\n')}
  // Utilities exposed for host app inline usage
${utilNames.map(n => `  window.AlloModules.${n} = ${n};`).join('\n')}
  window.AlloModules.AdventureModule = true;

  console.log('[AdventureModule] ${componentNames.length} components + ${utilNames.length} utilities registered');
})();
`;

const finalModule = iifeHeader + transformed + iifeFooter;

// ── Write output ──
fs.writeFileSync(OUTPUT, finalModule, 'utf-8');
console.log(`\n📄 Written: ${path.relative(__dirname, OUTPUT)} (${finalModule.split('\n').length} lines)`);

fs.writeFileSync(OUTPUT_PUBLIC, finalModule, 'utf-8');
console.log(`📄 Written: ${path.relative(__dirname, OUTPUT_PUBLIC)}`);

console.log(`\n✅ adventure_module.js built successfully with ${componentNames.length} components + ${utilNames.length} utilities`);
