#!/usr/bin/env node
/**
 * Build anchor_charts_module.js from anchor_charts_source.jsx
 *
 * Usage: node _build_anchor_charts_module.js
 *
 * Mirrors _build_note_taking_templates_module.js. Compiles JSX → React.createElement
 * via esbuild, wraps in IIFE with a11y CSS + hand-drawn-font CDN link + React
 * aliases, writes anchor_charts_module.js + syncs to prismflow-deploy/public/.
 *
 * Per architectural directive: Anchor Charts lives as a standalone CDN module,
 * NOT inline in AlloFlowANTI.txt.
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'anchor_charts_source.jsx');
const OUTPUT = path.join(ROOT, 'anchor_charts_module.js');
const DEPLOY_OUT = path.join(ROOT, 'prismflow-deploy', 'public', 'anchor_charts_module.js');
const TMP = path.join(ROOT, '_tmp_anchor_charts_entry.jsx');

if (!fs.existsSync(SOURCE)) {
    console.error('[AnchorCharts] Source not found:', SOURCE);
    process.exit(1);
}

const source = fs.readFileSync(SOURCE, 'utf-8');

const entry = `
/* global React, useState, useEffect, useRef, useCallback, useMemo, useContext */

${source}

window.__anchorChartExports = { AnchorChartView, AnchorChartSection, AnchorChartCritiqueOverlay };
`;

fs.writeFileSync(TMP, entry, 'utf-8');

console.log('Compiling anchor_charts_source.jsx with esbuild...');
try {
    execSync(
        `npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`,
        { cwd: ROOT, stdio: 'inherit' }
    );
} catch (e) {
    console.error('[AnchorCharts] esbuild compilation failed');
    try { fs.unlinkSync(TMP); } catch (_) {}
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    .replace(/\/\*.*global.*\*\/\n/g, '')
    .replace(/window\.__anchorChartExports\s*=\s*\{[^}]+\};?\s*/, '')
    .trim();

try { fs.unlinkSync(TMP); } catch (_) {}
try { fs.unlinkSync(TMP + '.compiled.js'); } catch (_) {}

const outputCode = `/**
 * AlloFlow Anchor Charts Module
 *
 * Contains: AnchorChartView, AnchorChartSection, AnchorChartCritiqueOverlay
 *
 * EL Education classroom-staple visual reference. Hand-drawn aesthetic.
 * AI-generated structure, student/teacher-refined, peer-critiqued via the
 * annotation suite's I-notice / I-wonder protocol.
 *
 * Auto-generated by _build_anchor_charts_module.js — DO NOT EDIT MANUALLY.
 * Source: anchor_charts_source.jsx
 */
(function() {
  'use strict';
  if (!document.getElementById("anchor-charts-module-a11y")) {
    var _s = document.createElement("style");
    _s.id = "anchor-charts-module-a11y";
    _s.textContent = "@media (prefers-reduced-motion: reduce) { *, *::before, *::after { animation-duration: 0.01ms !important; animation-iteration-count: 1 !important; transition-duration: 0.01ms !important; } } @media print { .ac-no-print { display: none !important; } textarea, input { border: none !important; background: white !important; } }";
    document.head.appendChild(_s);
  }
  // Lazy-load the hand-drawn display fonts (Patrick Hand + Permanent Marker)
  // from Google Fonts. Only injected once; safe if the user is offline (the
  // CSS @font-face falls back to the system "cursive" stack defined inline).
  if (!document.getElementById("anchor-charts-fonts")) {
    var _fl = document.createElement("link");
    _fl.id = "anchor-charts-fonts";
    _fl.rel = "stylesheet";
    _fl.href = "https://fonts.googleapis.com/css2?family=Patrick+Hand&family=Permanent+Marker&family=Caveat:wght@400;700&display=swap";
    document.head.appendChild(_fl);
  }
  (function() {
    if (document.getElementById('allo-live-anchor-charts')) return;
    var liveRegion = document.createElement('div');
    liveRegion.id = 'allo-live-anchor-charts';
    liveRegion.setAttribute('aria-live', 'polite');
    liveRegion.setAttribute('aria-atomic', 'true');
    liveRegion.setAttribute('role', 'status');
    liveRegion.className = 'sr-only';
    liveRegion.style.cssText = 'position:absolute;width:1px;height:1px;padding:0;margin:-1px;overflow:hidden;clip:rect(0,0,0,0);border:0';
    document.body.appendChild(liveRegion);
  })();

  if (window.__anchorChartsModuleLoaded) {
    console.warn('[AnchorChartsModule] Already loaded — skipping');
    return;
  }
  window.__anchorChartsModuleLoaded = true;

  var React = window.React;
  if (!React) { console.error('[AnchorChartsModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  // ═══════════════════════════════════════════════════════════════
  // ANCHOR CHART COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

${compiled}


  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.AnchorChartView = AnchorChartView;
  window.AlloModules.AnchorChartSection = AnchorChartSection;
  window.AlloModules.AnchorChartCritiqueOverlay = AnchorChartCritiqueOverlay;
  window.AlloModules.AnchorChartsModule = true;

  console.log('[AnchorChartsModule] 3 components registered');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
try {
    if (!fs.existsSync(path.dirname(DEPLOY_OUT))) {
        fs.mkdirSync(path.dirname(DEPLOY_OUT), { recursive: true });
    }
    fs.writeFileSync(DEPLOY_OUT, outputCode, 'utf-8');
} catch (e) {
    console.warn('[AnchorCharts] Could not sync to prismflow-deploy/public/:', e.message);
}
const lineCount = outputCode.split('\n').length;
console.log(`[AnchorCharts] Built ${OUTPUT} (${lineCount} lines)`);
console.log(`[AnchorCharts] Synced to ${DEPLOY_OUT}`);
