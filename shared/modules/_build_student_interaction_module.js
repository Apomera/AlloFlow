#!/usr/bin/env node
/**
 * Build script: StudentSubmitModal + DraftFeedbackInterface → student_interaction_module.js
 *
 * Reads JSX source, transforms via esbuild, wraps in IIFE with
 * dependency injection, outputs CDN-ready module.
 */

const fs = require('fs');
const { execSync } = require('child_process');
const path = require('path');

const SOURCE_JSX = path.join(__dirname, 'student_interaction_source.jsx');
const OUTPUT = path.join(__dirname, 'student_interaction_module.js');
const OUTPUT_PUBLIC = path.join(__dirname, 'prismflow-deploy', 'public', 'student_interaction_module.js');
const TEMP = path.join(__dirname, '_temp_student_interaction.jsx');

console.log('📦 Building student_interaction_module.js...\n');

if (!fs.existsSync(SOURCE_JSX)) {
    console.error('❌ student_interaction_source.jsx not found');
    process.exit(1);
}

const jsxSource = fs.readFileSync(SOURCE_JSX, 'utf-8');
console.log(`   Read ${jsxSource.split('\n').length} lines from student_interaction_source.jsx`);

const preamble = `/* eslint-disable */
// Auto-extracted Student Interaction components — DO NOT EDIT MANUALLY
// Generated by _build_student_interaction_module.js
`;

fs.writeFileSync(TEMP, preamble + jsxSource, 'utf-8');

console.log('   Running esbuild JSX transform...');
let transformed;
try {
    transformed = execSync(
        `npx esbuild "${TEMP}" --jsx=transform --bundle=false --target=es2020`,
        { encoding: 'utf-8', maxBuffer: 1024 * 1024 * 50 }
    );
} catch (e) {
    console.error('❌ esbuild failed:', e.message);
    if (e.stderr) console.error(e.stderr);
    process.exit(1);
}

fs.unlinkSync(TEMP);
console.log('   ✅ JSX transformed successfully');

const componentNames = ['StudentSubmitModal', 'DraftFeedbackInterface'];
componentNames.forEach(name => {
    const pattern = new RegExp(`const ${name} = `, 'g');
    transformed = transformed.replace(pattern, `var ${name} = `);
});

const iconList = [
    'ArrowRight', 'BookOpen', 'CheckSquare', 'Download', 'History',
    'Lock', 'MapIcon', 'Quote', 'RefreshCw', 'Send', 'Star', 'Trophy'
];

const iifeHeader = `/**
 * AlloFlow Student Interaction Module
 *
 * Contains: ${componentNames.join(', ')}
 *
 * Auto-generated by _build_student_interaction_module.js — DO NOT EDIT MANUALLY.
 * Source: student_interaction_source.jsx
 */
(function() {
  'use strict';

  if (window.__studentInteractionModuleLoaded) {
    console.warn('[StudentInteractionModule] Already loaded — skipping');
    return;
  }
  window.__studentInteractionModuleLoaded = true;

  var React = window.React;
  if (!React) { console.error('[StudentInteractionModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  var LanguageContext = window.AlloLanguageContext;
  var _shared = window.__alloShared || {};
  var safeGetItem = _shared.safeGetItem || function(k) { try { return localStorage.getItem(k); } catch(e) { return null; } };
  var safeSetItem = _shared.safeSetItem || function(k,v) { try { localStorage.setItem(k,v); } catch(e) {} };
  var warnLog = _shared.warnLog || function() { console.warn.apply(console, arguments); };

  var _hooks = window.__alloHooks || {};
  var useFocusTrap = _hooks.useFocusTrap || function() {};

  var _icons = window.AlloIcons || {};
${iconList.map(ic => `  var ${ic} = _icons.${ic} || function() { return null; };`).join('\n')}

  // ═══════════════════════════════════════════════════════════════
  // STUDENT INTERACTION COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

`;

const iifeFooter = `

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
${componentNames.map(n => `  window.AlloModules.${n} = ${n};`).join('\n')}
  window.AlloModules.StudentInteractionModule = true;

  console.log('[StudentInteractionModule] ${componentNames.length} components registered');
})();
`;

const finalModule = iifeHeader + transformed + iifeFooter;

fs.writeFileSync(OUTPUT, finalModule, 'utf-8');
console.log(`\n📄 Written: ${path.relative(__dirname, OUTPUT)} (${finalModule.split('\n').length} lines)`);

fs.writeFileSync(OUTPUT_PUBLIC, finalModule, 'utf-8');
console.log(`📄 Written: ${path.relative(__dirname, OUTPUT_PUBLIC)}`);

console.log('\n✅ student_interaction_module.js built successfully');
