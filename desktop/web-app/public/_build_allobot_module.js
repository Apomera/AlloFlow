#!/usr/bin/env node
/**
 * Build allobot_module.js from allobot_source.jsx
 *
 * Usage: node _build_allobot_module.js
 *
 * 1. Reads allobot_source.jsx (JSX components: SpeechBubble, AlloBot, etc.)
 * 2. Compiles JSX → React.createElement via esbuild
 * 3. Wraps in IIFE with dependency resolution + registration
 * 4. Writes allobot_module.js
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'allobot_source.jsx');
const OUTPUT = path.join(ROOT, 'allobot_module.js');
const TMP = path.join(ROOT, '_tmp_allobot_entry.jsx');

if (!fs.existsSync(SOURCE)) {
    console.error('❌ Source not found:', SOURCE);
    process.exit(1);
}

const source = fs.readFileSync(SOURCE, 'utf-8');

// Create entry point that wraps source with module boilerplate
// This defines the free variables that the source code expects
const entry = `
// ── Entry wrapper — resolved by module IIFE at runtime ──
// These will be replaced by the IIFE preamble in the final output.
// esbuild just needs them declared so JSX compilation succeeds.
/* global React, useState, useEffect, useRef, useCallback, useMemo, useContext, useImperativeHandle */
/* global LanguageContext, safeGetItem, safeSetItem, warnLog, debugLog */
/* global getGlobalAudioContext, isGlobalMuted, Mic, Volume2, VolumeX, Settings, X */

${source}

// ── Export for registration ──
window.__alloBotExports = { SpeechBubble, AlloBot };
`;

fs.writeFileSync(TMP, entry, 'utf-8');

// Compile JSX → vanilla JS
console.log('🔨 Compiling allobot_source.jsx with esbuild...');
try {
    execSync(`npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`, {
        cwd: ROOT,
        stdio: 'inherit'
    });
} catch (e) {
    console.error('❌ esbuild compilation failed');
    fs.unlinkSync(TMP);
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    // Remove the entry wrapper comments and export line
    .replace(/\/\/ ── Entry wrapper.*\n/g, '')
    .replace(/\/\*.*global.*\*\/\n/g, '')
    .replace(/window\.__alloBotExports\s*=\s*\{[^}]+\};?\s*/, '')
    .trim();

// Clean up temp files
fs.unlinkSync(TMP);
fs.unlinkSync(TMP + '.compiled.js');

// Build final module with IIFE wrapper
const outputCode = `/**
 * AlloFlow AlloBot Module
 *
 * Contains: SpeechBubble, LandingDust, JetpackParticles,
 *           ReactionBubble, BotConfettiBurst, AlloBot
 *
 * Auto-generated by _build_allobot_module.js — DO NOT EDIT MANUALLY.
 * Source: allobot_source.jsx
 */
(function() {
  'use strict';
  // WCAG 4.1.3: Status live region for dynamic content announcements
  (function() {
    if (document.getElementById('allo-live-allobot')) return;
    var liveRegion = document.createElement('div');
    liveRegion.id = 'allo-live-allobot';
    liveRegion.setAttribute('aria-live', 'polite');
    liveRegion.setAttribute('aria-atomic', 'true');
    liveRegion.setAttribute('role', 'status');
    liveRegion.className = 'sr-only';
    liveRegion.style.cssText = 'position:absolute;width:1px;height:1px;padding:0;margin:-1px;overflow:hidden;clip:rect(0,0,0,0);border:0';
    document.body.appendChild(liveRegion);
  })();


  // ── Duplicate-load guard ──
  if (window.__alloBotModuleLoaded) {
    console.warn('[AlloBotModule] Already loaded — skipping');
    return;
  }
  window.__alloBotModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[AlloBotModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;
  var useImperativeHandle = React.useImperativeHandle;

  // ── App dependencies from window ──
  var LanguageContext = window.AlloLanguageContext;
  var _shared = window.__alloShared || {};
  var safeGetItem = _shared.safeGetItem || function(k) { try { return localStorage.getItem(k); } catch(e) { return null; } };
  var safeSetItem = _shared.safeSetItem || function(k,v) { try { localStorage.setItem(k,v); } catch(e) {} };
  var warnLog = _shared.warnLog || function() { console.warn.apply(console, arguments); };
  var debugLog = window.__alloDebugLog || function() {};
  var getGlobalAudioContext = window.getGlobalAudioContext || function() { return null; };
  var isGlobalMuted = window.__alloIsGlobalMuted || function() { return false; };

  // ── Style constants (mirrored from host app) ──
  var STYLE_ANIMATION_DELAY_HALF = { animationDelay: '0.5s' };
  var STYLE_POINTER_EVENTS_NONE = { pointerEvents: 'none' };
  var STYLE_IMAGE_PIXELATED = { imageRendering: 'pixelated' };
  var STYLE_TEXT_SHADOW_WHITE = { textShadow: '0 2px 4px rgba(255,255,255,1)' };

  // ── Lucide icons from host app ──
  var _icons = window.AlloIcons || {};
  var Mic = _icons.Mic || function() { return null; };
  var Volume2 = _icons.Volume2 || function() { return null; };
  var VolumeX = _icons.VolumeX || function() { return null; };
  var Settings = _icons.Settings || function() { return null; };
  var X = _icons.X || function() { return null; };

  // ═══════════════════════════════════════════════════════════════
  // COMPILED COMPONENTS
  // ═══════════════════════════════════════════════════════════════

${compiled}

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.SpeechBubble = (typeof SpeechBubble !== 'undefined') ? SpeechBubble : null;
  window.AlloModules.LandingDust = (typeof LandingDust !== 'undefined') ? LandingDust : null;
  window.AlloModules.JetpackParticles = (typeof JetpackParticles !== 'undefined') ? JetpackParticles : null;
  window.AlloModules.ReactionBubble = (typeof ReactionBubble !== 'undefined') ? ReactionBubble : null;
  window.AlloModules.BotConfettiBurst = (typeof BotConfettiBurst !== 'undefined') ? BotConfettiBurst : null;
  window.AlloModules.AlloBot = AlloBot;

  console.log('[AlloBotModule] AlloBot registered successfully');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
const lineCount = outputCode.split('\n').length;
console.log(`✅ Built ${OUTPUT} (${lineCount} lines)`);
console.log('   Components: SpeechBubble, LandingDust, JetpackParticles, ReactionBubble, BotConfettiBurst, AlloBot');
