#!/usr/bin/env node
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'view_live_lesson_run_source.jsx');
const OUTPUT = path.join(ROOT, 'view_live_lesson_run_module.js');
const DEPLOY_OUT = path.join(ROOT, 'desktop/web-app', 'public', 'view_live_lesson_run_module.js');
const TMP = path.join(ROOT, '_tmp_live_lesson_run_entry.jsx');

if (!fs.existsSync(SOURCE)) {
  console.error('Source not found:', SOURCE);
  process.exit(1);
}

const source = fs.readFileSync(SOURCE, 'utf-8');
const entry = `/* global React */\n\n${source}\n\nwindow.__liveLessonRunExports = { buildLiveLessonSteps, resolveLiveLessonIndex, adjacentLiveLessonIndex, buildLiveLessonAudiences, resolveLiveLessonAudience, summarizeLiveLessonDelivery, sanitizeLiveActivitySnapshot, upsertLiveActivitySnapshot, selectLiveActivityPulse, liveActivityKindLabel, liveActivityPhaseLabel, LiveLessonRunPanel };\n`;
fs.writeFileSync(TMP, entry, 'utf-8');
console.log('Compiling view_live_lesson_run_source.jsx with esbuild...');

try {
  execSync(`npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`, {
    cwd: ROOT,
    stdio: 'inherit',
  });
} catch (error) {
  console.error('esbuild failed');
  try { fs.unlinkSync(TMP); } catch (_) {}
  process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
  .replace(/\/\*.*global.*\*\/\n/g, '')
  .replace(/window\.__liveLessonRunExports\s*=\s*\{[^}]+\};?\s*/, '')
  .trim();

fs.unlinkSync(TMP);
fs.unlinkSync(TMP + '.compiled.js');

const outputCode = `/**
 * AlloFlow Live Lesson Run Module
 * Auto-generated by _build_view_live_lesson_run_module.js - DO NOT EDIT MANUALLY.
 * Source: view_live_lesson_run_source.jsx
 */
(function() {
  'use strict';
  if (window.AlloModules && window.AlloModules.LiveLessonRun) {
    console.log('[CDN] LiveLessonRun already loaded, skipping');
    return;
  }
  var React = window.React;
  if (!React) { console.error('[LiveLessonRun] React not found on window'); return; }

${compiled}

  window.AlloModules = window.AlloModules || {};
  window.AlloModules.LiveLessonRun = {
    buildLiveLessonSteps: buildLiveLessonSteps,
    resolveLiveLessonIndex: resolveLiveLessonIndex,
    adjacentLiveLessonIndex: adjacentLiveLessonIndex,
    buildLiveLessonAudiences: buildLiveLessonAudiences,
    resolveLiveLessonAudience: resolveLiveLessonAudience,
    summarizeLiveLessonDelivery: summarizeLiveLessonDelivery,
    sanitizeLiveActivitySnapshot: sanitizeLiveActivitySnapshot,
    upsertLiveActivitySnapshot: upsertLiveActivitySnapshot,
    selectLiveActivityPulse: selectLiveActivityPulse,
    liveActivityKindLabel: liveActivityKindLabel,
    liveActivityPhaseLabel: liveActivityPhaseLabel,
    LiveLessonRunPanel: LiveLessonRunPanel
  };
  console.log('[CDN] LiveLessonRun loaded');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
try {
  if (!fs.existsSync(path.dirname(DEPLOY_OUT))) {
    fs.mkdirSync(path.dirname(DEPLOY_OUT), { recursive: true });
  }
  fs.writeFileSync(DEPLOY_OUT, outputCode, 'utf-8');
} catch (error) {
  console.warn('Sync failed:', error.message);
}
console.log(`Built ${OUTPUT} (${outputCode.split('\n').length} lines)`);
