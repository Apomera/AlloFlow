#!/usr/bin/env node
/**
 * Build concept_pictionary_module.js from concept_pictionary_source.jsx
 *
 * Usage: node _build_concept_pictionary_module.js
 *
 * Mirrors _build_anchor_charts_module.js. Compiles JSX → React.createElement
 * via esbuild, wraps in IIFE with a11y CSS + React aliases, writes
 * concept_pictionary_module.js + syncs to prismflow-deploy/public/.
 *
 * The module exposes:
 *   window.AlloModules.ConceptPictionary = {
 *     HostView, GuestOverlay, PictionaryCanvas,
 *     PictionaryHost, PictionaryGuest, PEN_COLORS, ERASER_SENTINEL
 *   }
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'concept_pictionary_source.jsx');
const OUTPUT = path.join(ROOT, 'concept_pictionary_module.js');
const DEPLOY_OUT = path.join(ROOT, 'prismflow-deploy', 'public', 'concept_pictionary_module.js');
const TMP = path.join(ROOT, '_tmp_concept_pictionary_entry.jsx');

if (!fs.existsSync(SOURCE)) {
    console.error('[ConceptPictionary] Source not found:', SOURCE);
    process.exit(1);
}

const source = fs.readFileSync(SOURCE, 'utf-8');

const entry = `
/* global React, useState, useEffect, useRef, useCallback, useMemo, useContext */

${source}

window.__pictionaryExports = {
  PictionaryHostView,
  PictionaryGuestOverlay,
  PictionaryCanvas,
  DrawerToolbox,
  PictionaryHost,
  PictionaryGuest,
  PEN_COLORS,
  ERASER_SENTINEL,
};
`;

fs.writeFileSync(TMP, entry, 'utf-8');

console.log('Compiling concept_pictionary_source.jsx with esbuild...');
try {
    execSync(
        `npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`,
        { cwd: ROOT, stdio: 'inherit' }
    );
} catch (e) {
    console.error('[ConceptPictionary] esbuild compilation failed');
    try { fs.unlinkSync(TMP); } catch (_) {}
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    .replace(/\/\*.*global.*\*\/\n/g, '')
    .replace(/window\.__pictionaryExports\s*=\s*\{[^}]+\};?\s*/, '')
    .trim();

try { fs.unlinkSync(TMP); } catch (_) {}
try { fs.unlinkSync(TMP + '.compiled.js'); } catch (_) {}

const outputCode = `/**
 * AlloFlow Concept Pictionary Module
 *
 * Multi-drawer / multi-guesser live-session game. Strokes + guesses flow
 * peer-to-peer over WebRTC; only Firestore signaling docs + Tier-1 role
 * markers touch the backend. FERPA-by-design.
 *
 * Auto-generated by _build_concept_pictionary_module.js — DO NOT EDIT MANUALLY.
 * Source: concept_pictionary_source.jsx
 */
(function() {
  'use strict';
  if (!document.getElementById("concept-pictionary-module-a11y")) {
    var _s = document.createElement("style");
    _s.id = "concept-pictionary-module-a11y";
    _s.textContent = "@media (prefers-reduced-motion: reduce) { *, *::before, *::after { animation-duration: 0.01ms !important; animation-iteration-count: 1 !important; transition-duration: 0.01ms !important; } } @keyframes pic-drawer-pulse-kf { 0%,100% { transform: scale(1); opacity: 1; } 50% { transform: scale(1.4); opacity: 0.55; } } .pic-drawer-pulse { animation: pic-drawer-pulse-kf 0.9s ease-in-out infinite; }";
    document.head.appendChild(_s);
  }
  (function() {
    if (document.getElementById('allo-live-concept-pictionary')) return;
    var liveRegion = document.createElement('div');
    liveRegion.id = 'allo-live-concept-pictionary';
    liveRegion.setAttribute('aria-live', 'polite');
    liveRegion.setAttribute('aria-atomic', 'true');
    liveRegion.setAttribute('role', 'status');
    liveRegion.className = 'sr-only';
    liveRegion.style.cssText = 'position:absolute;width:1px;height:1px;padding:0;margin:-1px;overflow:hidden;clip:rect(0,0,0,0);border:0';
    document.body.appendChild(liveRegion);
  })();

  if (window.__conceptPictionaryModuleLoaded) {
    console.warn('[ConceptPictionaryModule] Already loaded — skipping');
    return;
  }
  window.__conceptPictionaryModuleLoaded = true;

  var React = window.React;
  if (!React) { console.error('[ConceptPictionaryModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  // ═══════════════════════════════════════════════════════════════
  // CONCEPT PICTIONARY (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

${compiled}


  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.ConceptPictionary = {
    HostView: PictionaryHostView,
    GuestOverlay: PictionaryGuestOverlay,
    PictionaryCanvas: PictionaryCanvas,
    DrawerToolbox: DrawerToolbox,
    PictionaryHost: PictionaryHost,
    PictionaryGuest: PictionaryGuest,
    PEN_COLORS: PEN_COLORS,
    ERASER_SENTINEL: ERASER_SENTINEL,
  };
  window.AlloModules.ConceptPictionaryModule = true;

  console.log('[ConceptPictionaryModule] Host + Guest + Canvas registered');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
try {
    if (!fs.existsSync(path.dirname(DEPLOY_OUT))) {
        fs.mkdirSync(path.dirname(DEPLOY_OUT), { recursive: true });
    }
    fs.writeFileSync(DEPLOY_OUT, outputCode, 'utf-8');
} catch (e) {
    console.warn('[ConceptPictionary] Could not sync to prismflow-deploy/public/:', e.message);
}
const lineCount = outputCode.split('\n').length;
console.log(`[ConceptPictionary] Built ${OUTPUT} (${lineCount} lines)`);
console.log(`[ConceptPictionary] Synced to ${DEPLOY_OUT}`);
