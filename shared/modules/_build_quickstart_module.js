#!/usr/bin/env node
/**
 * Build script: QuickStartWizard → quickstart_module.js
 *
 * Reads JSX source, transforms via esbuild, wraps in IIFE with
 * dependency injection, outputs CDN-ready module.
 */

const fs = require('fs');
const { execSync } = require('child_process');
const path = require('path');

const SOURCE_JSX = path.join(__dirname, 'quickstart_source.jsx');
const OUTPUT = path.join(__dirname, 'quickstart_module.js');
const OUTPUT_PUBLIC = path.join(__dirname, 'prismflow-deploy', 'public', 'quickstart_module.js');
const TEMP = path.join(__dirname, '_temp_quickstart.jsx');

console.log('📦 Building quickstart_module.js...\n');

if (!fs.existsSync(SOURCE_JSX)) {
    console.error('❌ quickstart_source.jsx not found');
    process.exit(1);
}

const jsxSource = fs.readFileSync(SOURCE_JSX, 'utf-8');
console.log(`   Read ${jsxSource.split('\n').length} lines from quickstart_source.jsx`);

// ── Write temp JSX file for esbuild ──
const preamble = `/* eslint-disable */
// Auto-extracted QuickStartWizard — DO NOT EDIT MANUALLY
// Generated by _build_quickstart_module.js
`;

fs.writeFileSync(TEMP, preamble + jsxSource, 'utf-8');
console.log(`   Temp JSX file: ${TEMP}`);

// ── Transform JSX via esbuild ──
console.log('   Running esbuild JSX transform...');
let transformed;
try {
    transformed = execSync(
        `npx esbuild "${TEMP}" --jsx=transform --bundle=false --target=es2020`,
        { encoding: 'utf-8', maxBuffer: 1024 * 1024 * 50 }
    );
} catch (e) {
    console.error('❌ esbuild failed:', e.message);
    if (e.stderr) console.error(e.stderr);
    process.exit(1);
}

// Clean up temp file
fs.unlinkSync(TEMP);
console.log('   ✅ JSX transformed successfully');

// ── Post-process: fix const → var for top-level component ──
transformed = transformed.replace(/const QuickStartWizard = /g, 'var QuickStartWizard = ');

// ── Icons used by QuickStartWizard ──
const iconList = [
    'AlignJustify', 'ArrowDown', 'ArrowRight', 'Brain', 'CheckCircle',
    'CheckCircle2', 'Download', 'ExternalLink', 'Globe', 'GraduationCap',
    'HelpCircle', 'Languages', 'Link', 'MessageSquare', 'Plus',
    'RefreshCw', 'Search', 'ShieldCheck', 'Sparkles', 'Type',
    'Upload', 'Wrench', 'X'
];

// ── Build IIFE wrapper ──
const iifeHeader = `/**
 * AlloFlow QuickStartWizard Module
 *
 * Auto-generated by _build_quickstart_module.js — DO NOT EDIT MANUALLY.
 * Source: quickstart_source.jsx
 */
(function() {
  'use strict';

  // ── Duplicate-load guard ──
  if (window.__quickstartModuleLoaded) {
    console.warn('[QuickStartWizard] Already loaded — skipping');
    return;
  }
  window.__quickstartModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[QuickStartWizard] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  // ── App dependencies from window ──
  var LanguageContext = window.AlloLanguageContext;
  var _shared = window.__alloShared || {};
  var safeGetItem = _shared.safeGetItem || function(k) { try { return localStorage.getItem(k); } catch(e) { return null; } };
  var safeSetItem = _shared.safeSetItem || function(k,v) { try { localStorage.setItem(k,v); } catch(e) {} };
  var warnLog = _shared.warnLog || function() { console.warn.apply(console, arguments); };

  // ── Shared hooks & utilities from host app ──
  var _hooks = window.__alloHooks || {};
  var useFocusTrap = _hooks.useFocusTrap || function() {};
  var fetchAndCleanUrl = (window.__alloUtils || {}).fetchAndCleanUrl || function() { return Promise.resolve(''); };
  var isGoogleRedirect = (window.__alloUtils || {}).isGoogleRedirect || function() { return false; };

  // ── Lucide icons from host app ──
  var _icons = window.AlloIcons || {};
${iconList.map(ic => `  var ${ic} = _icons.${ic} || function() { return null; };`).join('\n')}

  // ═══════════════════════════════════════════════════════════════
  // QUICKSTARTWIZARD COMPONENT (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

`;

const iifeFooter = `

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.QuickStartWizard = QuickStartWizard;

  console.log('[QuickStartWizard] Module registered successfully');
})();
`;

const finalModule = iifeHeader + transformed + iifeFooter;

// ── Write output ──
fs.writeFileSync(OUTPUT, finalModule, 'utf-8');
console.log(`\n📄 Written: ${path.relative(__dirname, OUTPUT)} (${finalModule.split('\n').length} lines)`);

fs.writeFileSync(OUTPUT_PUBLIC, finalModule, 'utf-8');
console.log(`📄 Written: ${path.relative(__dirname, OUTPUT_PUBLIC)}`);

console.log('\n✅ quickstart_module.js built successfully');
