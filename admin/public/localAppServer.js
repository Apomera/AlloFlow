/**
 * AlloFlow — Local App Server (B4.4)
 *
 * HTTP server that serves local-app/build/ for the local-only AlloFlow app.
 * Mirrors webAppServer.js but injects __alloLocalConfig (not ai_config.json).
 *
 * Config source: ~/.alloflow/local_config.json
 * Inject target: window.__alloLocalConfig in every served index.html
 *
 * Used by admin/public/main.js to serve the local app alongside the web app.
 */

'use strict';

const http = require('http');
const fs   = require('fs');
const path = require('path');
const os   = require('os');

let _server     = null;
let _serverPort = 3730;

const LOCAL_CONFIG_FILE = path.join(os.homedir(), '.alloflow', 'local_config.json');

const MIME_TYPES = {
    '.html': 'text/html; charset=utf-8',
    '.css':  'text/css; charset=utf-8',
    '.js':   'application/javascript; charset=utf-8',
    '.json': 'application/json; charset=utf-8',
    '.png':  'image/png',
    '.jpg':  'image/jpeg',
    '.jpeg': 'image/jpeg',
    '.gif':  'image/gif',
    '.svg':  'image/svg+xml',
    '.ico':  'image/x-icon',
    '.woff': 'font/woff',
    '.woff2':'font/woff2',
    '.ttf':  'font/ttf',
    '.map':  'application/json',
    '.wav':  'audio/wav',
    '.mp3':  'audio/mpeg',
    '.ogg':  'audio/ogg',
    '.wasm': 'application/wasm',
};

const isStaticExt = new Set([
    '.js', '.css', '.png', '.jpg', '.jpeg', '.gif', '.svg',
    '.ico', '.woff', '.woff2', '.ttf', '.map', '.wav', '.mp3',
    '.ogg', '.wasm',
]);

// ── Config helpers ────────────────────────────────────────────────────────────

/**
 * Build the __alloLocalConfig injection script.
 * Reads local_config.json and injects it into the HTML before the app loads.
 */
function buildLocalConfigScript() {
    const DEFAULT = {
        ollamaUrl:    'http://localhost:11434',
        sqliteUrl:    'http://localhost:3747',
        piperEnabled: true,
        defaultModel: 'llama3.2:3b',
    };

    let cfg = DEFAULT;
    try {
        if (fs.existsSync(LOCAL_CONFIG_FILE)) {
            const raw = fs.readFileSync(LOCAL_CONFIG_FILE, 'utf-8');
            cfg = { ...DEFAULT, ...JSON.parse(raw) };
        }
    } catch (err) {
        console.warn('[localApp] Failed to read local_config.json:', err.message);
    }

    return `<script>
/* AlloFlow Local Config Injection — generated by localAppServer.js */
window.__alloLocalConfig = ${JSON.stringify(cfg, null, 2)};
window.__alloLocalDBUrl  = window.__alloLocalConfig.sqliteUrl;
</script>`;
}

// ── Path resolution ───────────────────────────────────────────────────────────

/**
 * @param {boolean} isPackaged
 */
function resolveLocalAppDir(isPackaged) {
    if (isPackaged) {
        return path.join(process.resourcesPath, 'local-app');
    }
    // Dev mode — local-app/build/ relative to admin/public/
    return path.join(__dirname, '..', '..', 'local-app', 'build');
}

// ── Server ────────────────────────────────────────────────────────────────────

/**
 * Start the local app HTTP server.
 * @param {number}  port       Port to listen on (default 3730)
 * @param {boolean} isPackaged Electron packaging flag
 * @returns {Promise<number>}  Actual port the server is listening on
 */
function startLocalAppServer(port, isPackaged) {
    _serverPort = port || 3730;
    const appDir     = resolveLocalAppDir(isPackaged);
    const publicDir  = path.join(__dirname, '..', '..', 'local-app', 'public');

    if (!fs.existsSync(appDir)) {
        // Build dir doesn't exist yet — warn but don't fail.
        // Admin can trigger a build or it may be generated later.
        console.warn('[localApp] Build directory not found:', appDir);
        console.warn('[localApp] Run: node local_build.js  to build the local app');
    }

    const indexPath = path.join(appDir, 'index.html');

    return new Promise((resolve, reject) => {
        _server = http.createServer((req, res) => {
            res.setHeader('Access-Control-Allow-Origin',  '*');
            res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS');
            res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

            if (req.method === 'OPTIONS') {
                res.writeHead(204);
                return res.end();
            }

            let urlPath = (req.url || '/').split('?')[0];
            if (urlPath === '/') urlPath = '/index.html';

            // Prevent path traversal
            const safePath = path.normalize(urlPath).replace(/^(\.\.(\/|\\|$))+/, '');
            const filePath  = path.join(appDir, safePath);

            if (!filePath.startsWith(appDir)) {
                res.writeHead(403);
                return res.end('Forbidden');
            }

            const ext     = path.extname(filePath).toLowerCase();
            const isStatic = isStaticExt.has(ext);

            // ── Serve index.html with injected config ──────────────────────
            if (urlPath === '/index.html' || (!fs.existsSync(filePath) && !isStatic)) {
                // Check both build dir and public/ for index.html
                const htmlSource = fs.existsSync(indexPath) ? indexPath
                    : path.join(publicDir, 'index.html');

                if (!fs.existsSync(htmlSource)) {
                    res.writeHead(503, { 'Content-Type': 'text/html; charset=utf-8' });
                    return res.end(buildNotReadyPage());
                }

                try {
                    let html = fs.readFileSync(htmlSource, 'utf-8');
                    // Inject local config before first </head> or at start of <head>
                    const inject = buildLocalConfigScript();
                    if (html.includes('<head>')) {
                        html = html.replace('<head>', '<head>\n  ' + inject);
                    } else {
                        html = inject + '\n' + html;
                    }
                    res.writeHead(200, {
                        'Content-Type':  'text/html; charset=utf-8',
                        'Cache-Control': 'no-store, no-cache, must-revalidate',
                    });
                    return res.end(html);
                } catch (err) {
                    console.error('[localApp] Error serving index.html:', err.message);
                    res.writeHead(500);
                    return res.end('Internal Server Error');
                }
            }

            // ── Static assets: 404 if missing (no HTML fallback) ──────────
            if (isStatic && !fs.existsSync(filePath)) {
                // Try falling back to public/ for un-built assets (dev mode)
                const pubFallback = path.join(publicDir, safePath);
                if (fs.existsSync(pubFallback)) {
                    const content = fs.readFileSync(pubFallback);
                    res.writeHead(200, {
                        'Content-Type':  MIME_TYPES[ext] || 'application/octet-stream',
                        'Cache-Control': 'no-store, no-cache, must-revalidate',
                    });
                    return res.end(content);
                }
                res.writeHead(404);
                return res.end('Not Found');
            }

            // ── Serve static file ─────────────────────────────────────────
            try {
                const content    = fs.readFileSync(filePath);
                const contentType = MIME_TYPES[ext] || 'application/octet-stream';
                res.writeHead(200, {
                    'Content-Type':  contentType,
                    'Cache-Control': 'no-store, no-cache, must-revalidate',
                });
                return res.end(content);
            } catch (err) {
                res.writeHead(404);
                return res.end('Not Found');
            }
        });

        _server.on('error', (err) => {
            if (err.code === 'EADDRINUSE') {
                console.warn(`[localApp] Port ${_serverPort} in use, trying ${_serverPort + 1}`);
                _serverPort++;
                _server.listen(_serverPort, '127.0.0.1');
            } else {
                reject(err);
            }
        });

        _server.listen(_serverPort, '127.0.0.1', () => {
            console.log(`[localApp] Local app serving at http://localhost:${_serverPort}`);
            resolve(_serverPort);
        });
    });
}

/**
 * Stop the local app server.
 */
function stopLocalAppServer() {
    if (_server) {
        try { _server.close(); } catch {}
        _server = null;
        console.log('[localApp] Local app server stopped');
    }
}

/** Returns the current local app port. */
function getLocalAppPort() {
    return _serverPort;
}

