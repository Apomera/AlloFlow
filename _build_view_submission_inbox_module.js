#!/usr/bin/env node
/**
 * Build view_submission_inbox_module.js from view_submission_inbox_source.jsx
 * (Phase 2 of the offline-HTML submission system, May 11 2026.)
 */
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'view_submission_inbox_source.jsx');
const OUTPUT = path.join(ROOT, 'view_submission_inbox_module.js');
const DEPLOY_OUT = path.join(ROOT, 'prismflow-deploy', 'public', 'view_submission_inbox_module.js');
const TMP = path.join(ROOT, '_tmp_submission_inbox_entry.jsx');

if (!fs.existsSync(SOURCE)) { console.error('Source not found:', SOURCE); process.exit(1); }

const source = fs.readFileSync(SOURCE, 'utf-8');
const entry = `/* global React, useState, useRef */\n\n${source}\n\nwindow.__submissionInboxExports = { SubmissionInbox };\n`;
fs.writeFileSync(TMP, entry, 'utf-8');

console.log('Compiling view_submission_inbox_source.jsx with esbuild...');
try {
    execSync(`npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`, { cwd: ROOT, stdio: 'inherit' });
} catch (e) {
    console.error('esbuild failed');
    try { fs.unlinkSync(TMP); } catch(_) {}
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    .replace(/\/\*.*global.*\*\/\n/g, '')
    .replace(/window\.__submissionInboxExports\s*=\s*\{[^}]+\};?\s*/, '')
    .trim();

fs.unlinkSync(TMP);
fs.unlinkSync(TMP + '.compiled.js');

const outputCode = `/**
 * AlloFlow Submission Inbox Module
 *
 * Auto-generated by _build_view_submission_inbox_module.js — DO NOT EDIT MANUALLY.
 * Source: view_submission_inbox_source.jsx
 *
 * Phase 2 of the offline-HTML worksheet submission system. Provides the
 * teacher-facing modal for batch-decrypting student submissions using the
 * class private key. Phase 3 (AI rubric grading + gradebook synthesis) will
 * extend this view with grading actions.
 */
(function() {
  'use strict';
  if (window.AlloModules && window.AlloModules.SubmissionInbox) {
    console.log('[CDN] SubmissionInbox already loaded, skipping');
    return;
  }
  var React = window.React;
  if (!React) { console.error('[SubmissionInbox] React not found on window'); return; }
  var useState = React.useState, useRef = React.useRef;

${compiled}

  window.AlloModules = window.AlloModules || {};
  window.AlloModules.SubmissionInbox = { SubmissionInbox: SubmissionInbox };
  console.log('[CDN] SubmissionInbox loaded');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
fs.writeFileSync(DEPLOY_OUT, outputCode, 'utf-8');
console.log(`Built ${OUTPUT} (${outputCode.split('\n').length} lines)`);
console.log(`Synced to ${DEPLOY_OUT}`);
