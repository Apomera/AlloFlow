#!/usr/bin/env node
/**
 * Build script: WordSoundsGenerator + WordSoundsReviewPanel → word_sounds_setup_module.js
 *
 * Contains: WordSoundsGenerator (phonics word game setup/configuration),
 *           WordSoundsReviewPanel (session review with phoneme guides)
 *
 * Reads JSX source, transforms via esbuild, wraps in IIFE with
 * dependency injection, outputs CDN-ready module.
 */

const fs = require('fs');
const { execSync } = require('child_process');
const path = require('path');

const SOURCE_JSX = path.join(__dirname, 'word_sounds_setup_source.jsx');
const OUTPUT = path.join(__dirname, 'word_sounds_setup_module.js');
const OUTPUT_PUBLIC = path.join(__dirname, 'prismflow-deploy', 'public', 'word_sounds_setup_module.js');
const TEMP = path.join(__dirname, '_temp_ws_setup.jsx');

console.log('📦 Building word_sounds_setup_module.js...\n');

if (!fs.existsSync(SOURCE_JSX)) {
    console.error('❌ word_sounds_setup_source.jsx not found');
    process.exit(1);
}

const jsxSource = fs.readFileSync(SOURCE_JSX, 'utf-8');
console.log(`   Read ${jsxSource.split('\n').length} lines from word_sounds_setup_source.jsx`);

// ── Write temp JSX file for esbuild ──
const preamble = `/* eslint-disable */
// Auto-extracted WordSounds setup components — DO NOT EDIT MANUALLY
// Generated by _build_word_sounds_setup_module.js
`;

fs.writeFileSync(TEMP, preamble + jsxSource, 'utf-8');
console.log(`   Temp JSX file: ${TEMP}`);

// ── Transform JSX via esbuild ──
console.log('   Running esbuild JSX transform...');
let transformed;
try {
    transformed = execSync(
        `npx esbuild "${TEMP}" --jsx=transform --bundle=false --target=es2020`,
        { encoding: 'utf-8', maxBuffer: 1024 * 1024 * 50 }
    );
} catch (e) {
    console.error('❌ esbuild failed:', e.message);
    if (e.stderr) console.error(e.stderr);
    process.exit(1);
}

// Clean up temp file
fs.unlinkSync(TEMP);
console.log('   ✅ JSX transformed successfully');

// ── Post-process: fix const/let → var for top-level declarations ──
const topLevelNames = ['WordSoundsGenerator', 'WordSoundsReviewPanel'];
topLevelNames.forEach(name => {
    // Match both `const Name = ` and indented `    const Name = `
    const pattern = new RegExp(`(const|let) ${name} = `, 'g');
    transformed = transformed.replace(pattern, `var ${name} = `);
});

// ── Icons used by word sounds setup components ──
const iconList = [
    'Ban', 'BookOpen', 'Check', 'CheckCircle2', 'ChevronDown', 'ChevronLeft',
    'Edit2', 'Eye', 'GripVertical', 'ImageIcon', 'Layers', 'Loader2',
    'Maximize2', 'Minimize', 'Palette', 'Play', 'PlayCircle', 'RefreshCw',
    'Send', 'Sparkles', 'Volume2'
];

// ── Build IIFE wrapper ──
const iifeHeader = `/**
 * AlloFlow Word Sounds Setup Module
 *
 * Contains: WordSoundsGenerator, WordSoundsReviewPanel
 *
 * Auto-generated by _build_word_sounds_setup_module.js — DO NOT EDIT MANUALLY.
 * Source: word_sounds_setup_source.jsx
 */
(function() {
  'use strict';

  // ── Duplicate-load guard ──
  if (window.__wordSoundsSetupModuleLoaded) {
    console.warn('[WordSoundsSetupModule] Already loaded — skipping');
    return;
  }
  window.__wordSoundsSetupModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[WordSoundsSetupModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  // ── App dependencies from window ──
  var LanguageContext = window.AlloLanguageContext;
  var _shared = window.__alloShared || {};
  var warnLog = _shared.warnLog || function() { console.warn.apply(console, arguments); };
  var debugLog = window.__alloDebugLog || function() {};
  var getGlobalAudioContext = window.getGlobalAudioContext || function() { return null; };

  // ── Probe bank globals (read-only, loaded by host app) ──
  // WordSoundsReviewPanel reads these for probe mode
  // They are set on window by loadPsychometricProbes() in the host app

  // ── Lucide icons from host app ──
  var _icons = window.AlloIcons || {};
${iconList.map(ic => `  var ${ic} = _icons.${ic} || function() { return null; };`).join('\n')}

  // ═══════════════════════════════════════════════════════════════
  // WORD SOUNDS SETUP COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

`;

const iifeFooter = `

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.WordSoundsGenerator = WordSoundsGenerator;
  window.AlloModules.WordSoundsReviewPanel = WordSoundsReviewPanel;
  window.AlloModules.WordSoundsSetupModule = true;

  // Update the global reference used by the WordSoundsModal CDN module
  window.WordSoundsReviewPanel = WordSoundsReviewPanel;

  console.log('[WordSoundsSetupModule] WordSoundsGenerator + WordSoundsReviewPanel registered');
})();
`;

const finalModule = iifeHeader + transformed + iifeFooter;

// ── Write output ──
fs.writeFileSync(OUTPUT, finalModule, 'utf-8');
console.log(`\n📄 Written: ${path.relative(__dirname, OUTPUT)} (${finalModule.split('\n').length} lines)`);

fs.writeFileSync(OUTPUT_PUBLIC, finalModule, 'utf-8');
console.log(`📄 Written: ${path.relative(__dirname, OUTPUT_PUBLIC)}`);

console.log('\n✅ word_sounds_setup_module.js built successfully');
