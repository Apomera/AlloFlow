#!/usr/bin/env node
/**
 * Build script: Extract game components from AlloFlowANTI.txt → games_module.js
 *
 * Reads game component source, transforms JSX → React.createElement via esbuild,
 * wraps in IIFE with dependency injection, outputs CDN-ready module.
 */

const fs = require('fs');
const { execSync } = require('child_process');
const path = require('path');

const SOURCE = path.join(__dirname, 'AlloFlowANTI.txt');
const SOURCE_JSX = path.join(__dirname, 'games_source.jsx');
const OUTPUT = path.join(__dirname, 'games_module.js');
const OUTPUT_PUBLIC = path.join(__dirname, 'prismflow-deploy', 'public', 'games_module.js');
const TEMP = path.join(__dirname, '_temp_games.jsx');
const USE_SOURCE_JSX = process.argv.includes('--source') || fs.existsSync(SOURCE_JSX);

// ── Game boundaries (1-indexed, inclusive) ──
const GAMES = [
    { name: 'MemoryGame',       start: 10684, end: 10971 },
    { name: 'MatchingGame',     start: 10990, end: 11348 },
    { name: 'TimelineGame',     start: 11349, end: 11671 },
    { name: 'ConceptSortGame',  start: 11672, end: 12011 },
    { name: 'VennGame',         start: 12012, end: 12344 },
    { name: 'CrosswordGame',    start: 12345, end: 12768 },
    { name: 'SyntaxScramble',   start: 12769, end: 12924 },
    { name: 'BingoGame',        start: 12925, end: 13540 },
    { name: 'StudentBingoGame', start: 13541, end: 13706 },
    { name: 'WordScrambleGame', start: 13707, end: 13865 },
];

console.log('📦 Building games_module.js...\n');

let jsxSource = '';
let totalLines = 0;

if (USE_SOURCE_JSX) {
    // ── Read from games_source.jsx directly ──
    console.log(`   Source mode: games_source.jsx`);
    jsxSource = fs.readFileSync(SOURCE_JSX, 'utf-8');
    totalLines = jsxSource.split('\n').length;
    console.log(`   Read ${totalLines} lines from games_source.jsx`);
} else {
    // ── Extract from AlloFlowANTI.txt by line ranges ──
    const allLines = fs.readFileSync(SOURCE, 'utf-8').split('\n');
    console.log(`   Source: ${allLines.length} lines`);
    GAMES.forEach(g => {
        const chunk = allLines.slice(g.start - 1, g.end);
        jsxSource += `\n// ═══ ${g.name} (lines ${g.start}-${g.end}) ═══\n`;
        jsxSource += chunk.join('\n') + '\n';
        totalLines += chunk.length;
        console.log(`   ✅ ${g.name}: ${chunk.length} lines (${g.start}-${g.end})`);
    });
    console.log(`\n   Total game source: ${totalLines} lines`);
}

// ── Write temp JSX file for esbuild ──
// We need to declare the variables that esbuild's JSX transform will reference.
// esbuild doesn't care about undefined variables in non-bundle mode — it just
// transforms JSX syntax. But we add declarations to suppress any warnings.
const preamble = `/* eslint-disable */
// Auto-extracted game components — DO NOT EDIT MANUALLY
// Generated by _build_games_module.js
`;

fs.writeFileSync(TEMP, preamble + jsxSource, 'utf-8');
console.log(`\n   Temp JSX file: ${TEMP}`);

// ── Transform JSX via esbuild ──
console.log('   Running esbuild JSX transform...');
let transformed;
try {
    transformed = execSync(
        `npx esbuild "${TEMP}" --jsx=transform --bundle=false --target=es2020`,
        { encoding: 'utf-8', maxBuffer: 1024 * 1024 * 50 }
    );
} catch (e) {
    console.error('❌ esbuild failed:', e.message);
    if (e.stderr) console.error(e.stderr);
    process.exit(1);
}

// Clean up temp file
fs.unlinkSync(TEMP);
console.log('   ✅ JSX transformed successfully');

// ── Post-process: fix React.memo const declarations ──
// esbuild output uses `const` for top-level declarations.
// We need to change game component declarations to `var` so they're
// accessible at the bottom of the IIFE for registration.
GAMES.forEach(g => {
    // Match: const GameName = React.memo( or const GameName = /* @__PURE__ */ React.memo(
    const pattern = new RegExp(`const ${g.name} = `, 'g');
    transformed = transformed.replace(pattern, `var ${g.name} = `);
});

// Also fix any createDerangement or other local const that might conflict
// (these are fine as const inside function scope, but let's be safe)

// ── Build IIFE wrapper ──
const iconList = [
    'ArrowDown', 'ArrowRight', 'ArrowUp', 'Brain', 'CheckCircle2',
    'Eye', 'EyeOff', 'Filter', 'Gamepad2', 'GitMerge', 'GripVertical',
    'HelpCircle', 'ImageIcon', 'Layout', 'ListOrdered', 'Maximize',
    'Mic', 'Minimize', 'MonitorPlay', 'Plus', 'Printer', 'RefreshCw',
    'Search', 'Star', 'StopCircle', 'Trophy', 'Type', 'Volume2', 'X'
];

const gameNames = GAMES.map(g => g.name);

const iifeHeader = `/**
 * AlloFlow Games Bundle Module
 *
 * Contains: ${gameNames.join(', ')}
 *
 * Auto-generated by _build_games_module.js — DO NOT EDIT MANUALLY.
 * Source: AlloFlowANTI.txt lines ${GAMES[0].start}-${GAMES[GAMES.length-1].end}
 */
(function() {
  'use strict';

  // ── Duplicate-load guard ──
  if (window.__gamesModuleLoaded) {
    console.warn('[GamesBundle] Already loaded — skipping');
    return;
  }
  window.__gamesModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[GamesBundle] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  // ── App dependencies from window ──
  var LanguageContext = window.AlloLanguageContext;
  var fisherYatesShuffle = window.fisherYatesShuffle;
  var getGlobalAudioContext = window.getGlobalAudioContext || function() { return null; };
  var warnLog = (typeof window.__alloWarnLog === 'function') ? window.__alloWarnLog : function() { console.warn.apply(console, arguments); };

  // ── Local utilities ──
  var scrambleWord = function(word) {
    if (!word || word.length < 2) return word;
    var arr = word.split('');
    for (var i = arr.length - 1; i > 0; i--) {
      var j = Math.floor(Math.random() * (i + 1));
      var tmp = arr[i]; arr[i] = arr[j]; arr[j] = tmp;
    }
    var result = arr.join('');
    return result === word ? scrambleWord(word) : result;
  };

  // ── Lucide icons from host app ──
  var _icons = window.AlloIcons || {};
${iconList.map(ic => `  var ${ic} = _icons.${ic} || function() { return null; };`).join('\n')}

  // ── Shared components from host app ──
  var _mods = window.AlloModules || {};
  var ConfettiExplosion = _mods.ConfettiExplosion || function() { return null; };
  var MatchVisuals = _mods.MatchVisuals || function() { return null; };

  // ═══════════════════════════════════════════════════════════════
  // GAME COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

`;

const iifeFooter = `

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
${gameNames.map(n => `  window.AlloModules.${n} = ${n};`).join('\n')}

  console.log('[GamesBundle] ${gameNames.length} games registered:', ${JSON.stringify(gameNames)});
})();
`;

const finalModule = iifeHeader + transformed + iifeFooter;

// ── Write output ──
fs.writeFileSync(OUTPUT, finalModule, 'utf-8');
console.log(`\n📄 Written: ${path.relative(__dirname, OUTPUT)} (${finalModule.split('\n').length} lines)`);

fs.writeFileSync(OUTPUT_PUBLIC, finalModule, 'utf-8');
console.log(`📄 Written: ${path.relative(__dirname, OUTPUT_PUBLIC)}`);

console.log(`\n✅ games_module.js built successfully with ${gameNames.length} games`);
console.log(`   Games: ${gameNames.join(', ')}`);
