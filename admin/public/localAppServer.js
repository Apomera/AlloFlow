/**
 * Handle /api/ai/chat proxy — forward requests to Gemini with server-side token.
 * @param {http.ServerRequest} req
 * @param {http.ServerResponse} res
 * @param {object} body Parsed JSON body
 */
async function handleAiChatApi(req, res, body) {
    try {
        const { provider, messages, model } = body;
        
        if (!provider || provider === 'gemini') {
            const apiKey = loadGeminiApiKey();
            const token  = apiKey ? null : loadGeminiToken();
            if (!apiKey && !token) {
                res.writeHead(401, { 'Content-Type': 'application/json' });
                return res.end(JSON.stringify({ error: 'Gemini not configured — add an API key in Settings → AI Config' }));
            }

            const geminiUrl = apiKey
                ? `https://generativelanguage.googleapis.com/v1beta/openai/chat/completions?key=${encodeURIComponent(apiKey)}`
                : 'https://generativelanguage.googleapis.com/v1beta/openai/chat/completions';
            const geminiHeaders = { 'Content-Type': 'application/json' };
            if (token) geminiHeaders['Authorization'] = `Bearer ${token}`;

            // Call Gemini OpenAI-compatible API
            const geminiRes = await fetch(geminiUrl, {
                method: 'POST',
                headers: geminiHeaders,
                body: JSON.stringify({ messages, model: model || 'gemini-2.0-flash' }),
            });

            const data = await geminiRes.json();
            res.writeHead(geminiRes.status, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify(data));
        } else if (provider === 'copilot') {
            const token = loadCopilotToken();
            if (!token) {
                res.writeHead(401, { 'Content-Type': 'application/json' });
                return res.end(JSON.stringify({ error: 'Copilot token not available' }));
            }

            // Call Copilot API with server-side token
            const copilotRes = await fetch('https://api.copilot.microsoft.com/chat', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${token}`,
                },
                body: JSON.stringify({ messages }),
            });

            const data = await copilotRes.json();
            res.writeHead(copilotRes.status, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify(data));
        } else {
            res.writeHead(400, { 'Content-Type': 'application/json' });
            return res.end(JSON.stringify({ error: 'Unknown AI provider' }));
        }
    } catch (err) {
        console.error('[localApp:api] Error in chat proxy:', err.message);
        res.writeHead(500, { 'Content-Type': 'application/json' });
        return res.end(JSON.stringify({ error: 'Internal server error' }));
    }
}

