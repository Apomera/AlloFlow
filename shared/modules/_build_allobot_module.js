#!/usr/bin/env node
/**
 * Build script: AlloBot + helpers → allobot_module.js
 *
 * Includes: SpeechBubble, LandingDust, JetpackParticles, ReactionBubble,
 *           BotConfettiBurst, AlloBot
 *
 * Reads JSX source, transforms via esbuild, wraps in IIFE with
 * dependency injection, outputs CDN-ready module.
 */

const fs = require('fs');
const { execSync } = require('child_process');
const path = require('path');

const SOURCE_JSX = path.join(__dirname, 'allobot_source.jsx');
const OUTPUT = path.join(__dirname, 'allobot_module.js');
const OUTPUT_PUBLIC = path.join(__dirname, 'prismflow-deploy', 'public', 'allobot_module.js');
const TEMP = path.join(__dirname, '_temp_allobot.jsx');

console.log('📦 Building allobot_module.js...\n');

if (!fs.existsSync(SOURCE_JSX)) {
    console.error('❌ allobot_source.jsx not found');
    process.exit(1);
}

const jsxSource = fs.readFileSync(SOURCE_JSX, 'utf-8');
console.log(`   Read ${jsxSource.split('\n').length} lines from allobot_source.jsx`);

// ── Write temp JSX file for esbuild ──
const preamble = `/* eslint-disable */
// Auto-extracted AlloBot + helpers — DO NOT EDIT MANUALLY
// Generated by _build_allobot_module.js
`;

fs.writeFileSync(TEMP, preamble + jsxSource, 'utf-8');
console.log(`   Temp JSX file: ${TEMP}`);

// ── Transform JSX via esbuild ──
console.log('   Running esbuild JSX transform...');
let transformed;
try {
    transformed = execSync(
        `npx esbuild "${TEMP}" --jsx=transform --bundle=false --target=es2020`,
        { encoding: 'utf-8', maxBuffer: 1024 * 1024 * 50 }
    );
} catch (e) {
    console.error('❌ esbuild failed:', e.message);
    if (e.stderr) console.error(e.stderr);
    process.exit(1);
}

// Clean up temp file
fs.unlinkSync(TEMP);
console.log('   ✅ JSX transformed successfully');

// ── Post-process: fix const → var for top-level declarations ──
const topLevelNames = [
    'SpeechBubble', 'LandingDust', 'JetpackParticles',
    'ReactionBubble', 'BotConfettiBurst', 'AlloBot',
    'spokenEventIds', 'lastGlobalSpeech'
];
topLevelNames.forEach(name => {
    const pattern = new RegExp(`const ${name} = `, 'g');
    transformed = transformed.replace(pattern, `var ${name} = `);
});
// introFiredGlobal uses let
transformed = transformed.replace(/let introFiredGlobal = /g, 'var introFiredGlobal = ');

// ── Icons used by AlloBot ──
const iconList = [
    'Mic', 'MicOff', 'Settings', 'Volume2', 'VolumeX'
];

// ── Build IIFE wrapper ──
const iifeHeader = `/**
 * AlloFlow AlloBot Module
 *
 * Contains: SpeechBubble, LandingDust, JetpackParticles,
 *           ReactionBubble, BotConfettiBurst, AlloBot
 *
 * Auto-generated by _build_allobot_module.js — DO NOT EDIT MANUALLY.
 * Source: allobot_source.jsx
 */
(function() {
  'use strict';

  // ── Duplicate-load guard ──
  if (window.__alloBotModuleLoaded) {
    console.warn('[AlloBotModule] Already loaded — skipping');
    return;
  }
  window.__alloBotModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[AlloBotModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;
  var useImperativeHandle = React.useImperativeHandle;

  // ── App dependencies from window ──
  var LanguageContext = window.AlloLanguageContext;
  var _shared = window.__alloShared || {};
  var safeGetItem = _shared.safeGetItem || function(k) { try { return localStorage.getItem(k); } catch(e) { return null; } };
  var safeSetItem = _shared.safeSetItem || function(k,v) { try { localStorage.setItem(k,v); } catch(e) {} };
  var warnLog = _shared.warnLog || function() { console.warn.apply(console, arguments); };
  var debugLog = window.__alloDebugLog || function() {};
  var getGlobalAudioContext = window.getGlobalAudioContext || function() { return null; };
  var isGlobalMuted = window.__alloIsGlobalMuted || function() { return false; };

  // ── Lucide icons from host app ──
  var _icons = window.AlloIcons || {};
${iconList.map(ic => `  var ${ic} = _icons.${ic} || function() { return null; };`).join('\n')}

  // ═══════════════════════════════════════════════════════════════
  // ALLOBOT COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

`;

const iifeFooter = `

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.AlloBot = AlloBot;

  console.log('[AlloBotModule] AlloBot registered successfully');
})();
`;

const finalModule = iifeHeader + transformed + iifeFooter;

// ── Write output ──
fs.writeFileSync(OUTPUT, finalModule, 'utf-8');
console.log(`\n📄 Written: ${path.relative(__dirname, OUTPUT)} (${finalModule.split('\n').length} lines)`);

fs.writeFileSync(OUTPUT_PUBLIC, finalModule, 'utf-8');
console.log(`📄 Written: ${path.relative(__dirname, OUTPUT_PUBLIC)}`);

console.log('\n✅ allobot_module.js built successfully');
