#!/usr/bin/env node
/**
 * Build note_taking_templates_module.js from note_taking_templates_source.jsx
 *
 * Usage: node _build_note_taking_templates_module.js
 *
 * 1. Reads note_taking_templates_source.jsx (3 template view components +
 *    dispatcher: CornellNotesView, LabReportView, ReadingResponseView,
 *    NoteTakingView)
 * 2. Compiles JSX → React.createElement via esbuild
 * 3. Wraps in IIFE with the runtime preamble (a11y CSS, live region,
 *    React hook + icon aliases)
 * 4. Writes note_taking_templates_module.js + syncs to prismflow-deploy/public/
 *
 * Per architectural directive (memory: feedback_note_taking_templates_module.md):
 * Note-Taking Templates lives as a standalone CDN module, NOT inline in
 * AlloFlowANTI.txt.
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'note_taking_templates_source.jsx');
const OUTPUT = path.join(ROOT, 'note_taking_templates_module.js');
const DEPLOY_OUT = path.join(ROOT, 'prismflow-deploy', 'public', 'note_taking_templates_module.js');
const TMP = path.join(ROOT, '_tmp_note_taking_templates_entry.jsx');

if (!fs.existsSync(SOURCE)) {
    console.error('[NoteTaking] Source not found:', SOURCE);
    process.exit(1);
}

const source = fs.readFileSync(SOURCE, 'utf-8');

const entry = `
/* global React, useState, useEffect, useRef, useCallback, useMemo, useContext */
/* global LanguageContext, warnLog */

${source}

window.__noteTakingExports = { CornellNotesView, LabReportView, ReadingResponseView, NoteTakingView, NotebookOverlay };
`;

fs.writeFileSync(TMP, entry, 'utf-8');

console.log('Compiling note_taking_templates_source.jsx with esbuild...');
try {
    execSync(
        `npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`,
        { cwd: ROOT, stdio: 'inherit' }
    );
} catch (e) {
    console.error('[NoteTaking] esbuild compilation failed');
    try { fs.unlinkSync(TMP); } catch (_) {}
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    .replace(/\/\*.*global.*\*\/\n/g, '')
    .replace(/window\.__noteTakingExports\s*=\s*\{[^}]+\};?\s*/, '')
    .trim();

try { fs.unlinkSync(TMP); } catch (_) {}
try { fs.unlinkSync(TMP + '.compiled.js'); } catch (_) {}

const outputCode = `/**
 * AlloFlow Note-Taking Templates Module
 *
 * Contains: CornellNotesView, LabReportView, ReadingResponseView, NoteTakingView
 *
 * Multi-session note-taking resources with lesson-aware pre-population.
 * Persists to History so a student's notebook is the chronological subset
 * of history entries with type='note-taking'.
 *
 * Auto-generated by _build_note_taking_templates_module.js — DO NOT EDIT MANUALLY.
 * Source: note_taking_templates_source.jsx
 */
(function() {
  'use strict';
  if (!document.getElementById("note-taking-templates-module-a11y")) {
    var _s = document.createElement("style");
    _s.id = "note-taking-templates-module-a11y";
    _s.textContent = "@media (prefers-reduced-motion: reduce) { *, *::before, *::after { animation-duration: 0.01ms !important; animation-iteration-count: 1 !important; transition-duration: 0.01ms !important; } } @media print { .nt-no-print { display: none !important; } textarea, input { border: none !important; background: white !important; } }";
    document.head.appendChild(_s);
  }
  (function() {
    if (document.getElementById('allo-live-note-taking-templates')) return;
    var liveRegion = document.createElement('div');
    liveRegion.id = 'allo-live-note-taking-templates';
    liveRegion.setAttribute('aria-live', 'polite');
    liveRegion.setAttribute('aria-atomic', 'true');
    liveRegion.setAttribute('role', 'status');
    liveRegion.className = 'sr-only';
    liveRegion.style.cssText = 'position:absolute;width:1px;height:1px;padding:0;margin:-1px;overflow:hidden;clip:rect(0,0,0,0);border:0';
    document.body.appendChild(liveRegion);
  })();

  if (window.__noteTakingTemplatesModuleLoaded) {
    console.warn('[NoteTakingTemplatesModule] Already loaded — skipping');
    return;
  }
  window.__noteTakingTemplatesModuleLoaded = true;

  var React = window.React;
  if (!React) { console.error('[NoteTakingTemplatesModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  var LanguageContext = window.AlloLanguageContext;
  var _shared = window.__alloShared || {};
  var warnLog = _shared.warnLog || function() { console.warn.apply(console, arguments); };

  // ═══════════════════════════════════════════════════════════════
  // NOTE-TAKING TEMPLATE COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

${compiled}


  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.CornellNotesView = CornellNotesView;
  window.AlloModules.LabReportView = LabReportView;
  window.AlloModules.ReadingResponseView = ReadingResponseView;
  window.AlloModules.NoteTakingView = NoteTakingView;
  window.AlloModules.NotebookOverlay = NotebookOverlay;
  window.AlloModules.NoteTakingTemplatesModule = true;

  console.log('[NoteTakingTemplatesModule] 5 components registered');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
try {
    if (!fs.existsSync(path.dirname(DEPLOY_OUT))) {
        fs.mkdirSync(path.dirname(DEPLOY_OUT), { recursive: true });
    }
    fs.writeFileSync(DEPLOY_OUT, outputCode, 'utf-8');
} catch (e) {
    console.warn('[NoteTaking] Could not sync to prismflow-deploy/public/:', e.message);
}
const lineCount = outputCode.split('\n').length;
console.log(`[NoteTaking] Built ${OUTPUT} (${lineCount} lines)`);
console.log(`[NoteTaking] Synced to ${DEPLOY_OUT}`);
