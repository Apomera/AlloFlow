#!/usr/bin/env node
/**
 * Build seating_chart_module.js from seating_chart_source.jsx
 * (Teacher seating chart over the class roster — Ring 0+1, July 21 2026.)
 *
 * Mirrors _build_view_submission_inbox_module.js: esbuild JSX → createElement,
 * IIFE wrapper with a11y CSS + live region, writes seating_chart_module.js +
 * syncs to desktop/web-app/public/.
 */
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'seating_chart_source.jsx');
const OUTPUT = path.join(ROOT, 'seating_chart_module.js');
const DEPLOY_OUT = path.join(ROOT, 'desktop/web-app', 'public', 'seating_chart_module.js');
const TMP = path.join(ROOT, '_tmp_seating_chart_entry.jsx');

if (!fs.existsSync(SOURCE)) { console.error('Source not found:', SOURCE); process.exit(1); }

const source = fs.readFileSync(SOURCE, 'utf-8');
const entry = `/* global React, useState, useEffect, useRef */\n\n${source}\n\nwindow.__seatingChartExports = { SeatingChartPanel, normalizeSeating, buildTemplate, solveSeating, scoreAssignment, anchorGaps, buildPrintableSvg, nextId, makeRng, centerDist, normalizeConstraint, describeSeatForStudent, pushHistory, listPods, overlappingSeatIds, CONSTRAINT_TYPES, FURNITURE_KINDS, ROOM_W, ROOM_H, ADJ_DIST, NEAR_RADIUS, WINDOW_RADIUS };\n`;
fs.writeFileSync(TMP, entry, 'utf-8');

console.log('Compiling seating_chart_source.jsx with esbuild...');
try {
    execSync(`npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`, { cwd: ROOT, stdio: 'inherit' });
} catch (e) {
    console.error('esbuild failed');
    try { fs.unlinkSync(TMP); } catch(_) {}
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    .replace(/\/\*.*global.*\*\/\n/g, '')
    .replace(/window\.__seatingChartExports\s*=\s*\{[^}]+\};?\s*/, '')
    .trim();

fs.unlinkSync(TMP);
fs.unlinkSync(TMP + '.compiled.js');

const outputCode = `/**
 * AlloFlow Seating Chart Module
 *
 * Auto-generated by _build_seating_chart_module.js — DO NOT EDIT MANUALLY.
 * Source: seating_chart_source.jsx
 *
 * Teacher seating chart bound to rosterKey: draggable classroom map with
 * templates, click-assign seating, declared constraints (keep apart /
 * together / front / near teacher / near door / away from windows / fixed
 * seat), and a LOCAL deterministic solver (greedy + swap repair). No AI in
 * the loop; charts and constraints never leave the device.
 */
(function() {
  'use strict';
  if (window.AlloModules && window.AlloModules.SeatingChart) {
    console.log('[CDN] SeatingChart already loaded, skipping');
    return;
  }
  if (!document.getElementById("seating-chart-a11y")) {
    var _s = document.createElement("style");
    _s.id = "seating-chart-a11y";
    _s.textContent = "@media (prefers-reduced-motion: reduce) { *, *::before, *::after { animation-duration: 0.01ms !important; animation-iteration-count: 1 !important; transition-duration: 0.01ms !important; } }";
    document.head.appendChild(_s);
  }
  (function() {
    if (document.getElementById('allo-live-seating')) return;
    var liveRegion = document.createElement('div');
    liveRegion.id = 'allo-live-seating';
    liveRegion.setAttribute('aria-live', 'polite');
    liveRegion.setAttribute('aria-atomic', 'true');
    liveRegion.setAttribute('role', 'status');
    liveRegion.className = 'sr-only';
    liveRegion.style.cssText = 'position:absolute;width:1px;height:1px;padding:0;margin:-1px;overflow:hidden;clip:rect(0,0,0,0);border:0';
    document.body.appendChild(liveRegion);
  })();
  var React = window.React;
  if (!React) { console.error('[SeatingChart] React not found on window'); return; }
  var useState = React.useState, useEffect = React.useEffect, useRef = React.useRef;

${compiled}

  window.AlloModules = window.AlloModules || {};
  window.AlloModules.SeatingChart = {
    SeatingChartPanel: SeatingChartPanel,
    // BehaviorLens bridge (public contract): plain-English seat description
    // for ABC setting enrichment. Positional only unless opts.includeNeighbors.
    describeSeatForStudent: describeSeatForStudent,
    // Class Goals bridge (public contract): pods on the active layout for
    // team-scoped goals ('pod:<index>' resolves against the CURRENT layout).
    listPods: listPods,
    // Pure seams exposed for unit tests + future live-session overlay work.
    // Not part of the public contract.
    _testing: {
      describeSeatForStudent: describeSeatForStudent,
      pushHistory: pushHistory,
      listPods: listPods,
      overlappingSeatIds: overlappingSeatIds,
      normalizeSeating: normalizeSeating,
      normalizeConstraint: normalizeConstraint,
      buildTemplate: buildTemplate,
      solveSeating: solveSeating,
      scoreAssignment: scoreAssignment,
      anchorGaps: anchorGaps,
      buildPrintableSvg: buildPrintableSvg,
      nextId: nextId,
      makeRng: makeRng,
      centerDist: centerDist,
      CONSTRAINT_TYPES: CONSTRAINT_TYPES,
      FURNITURE_KINDS: FURNITURE_KINDS,
      ROOM_W: ROOM_W,
      ROOM_H: ROOM_H,
      ADJ_DIST: ADJ_DIST,
      NEAR_RADIUS: NEAR_RADIUS,
      WINDOW_RADIUS: WINDOW_RADIUS
    }
  };
  console.log('[CDN] SeatingChart loaded');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
try {
    if (!fs.existsSync(path.dirname(DEPLOY_OUT))) fs.mkdirSync(path.dirname(DEPLOY_OUT), { recursive: true });
    fs.writeFileSync(DEPLOY_OUT, outputCode, 'utf-8');
    console.log(`Synced to ${DEPLOY_OUT}`);
} catch (e) {
    console.warn('[SeatingChart] Could not sync to desktop/web-app/public/:', e.message);
}
console.log(`Built ${OUTPUT} (${outputCode.split('\n').length} lines)`);
