#!/usr/bin/env node
/**
 * Build script: Teacher analytics bundle → teacher_module.js
 *
 * Contains: RosterKeyPanel, SimpleBarChart, SimpleDonutChart,
 *           LongitudinalProgressChart, ConfettiEffect,
 *           StudentEscapeRoomOverlay, TeacherLiveQuizControls,
 *           calculateAnalyticsMetrics, LearnerProgressView, TeacherDashboard
 *
 * Reads JSX source, transforms via esbuild, wraps in IIFE with
 * dependency injection, outputs CDN-ready module.
 */

const fs = require('fs');
const { execSync } = require('child_process');
const path = require('path');

const SOURCE_JSX = path.join(__dirname, 'teacher_source.jsx');
const OUTPUT = path.join(__dirname, 'teacher_module.js');
const OUTPUT_PUBLIC = path.join(__dirname, 'prismflow-deploy', 'public', 'teacher_module.js');
const TEMP = path.join(__dirname, '_temp_teacher.jsx');

console.log('📦 Building teacher_module.js...\n');

if (!fs.existsSync(SOURCE_JSX)) {
    console.error('❌ teacher_source.jsx not found');
    process.exit(1);
}

const jsxSource = fs.readFileSync(SOURCE_JSX, 'utf-8');
console.log(`   Read ${jsxSource.split('\n').length} lines from teacher_source.jsx`);

// ── Write temp JSX file for esbuild ──
const preamble = `/* eslint-disable */
// Auto-extracted Teacher analytics bundle — DO NOT EDIT MANUALLY
// Generated by _build_teacher_module.js
`;

fs.writeFileSync(TEMP, preamble + jsxSource, 'utf-8');
console.log(`   Temp JSX file: ${TEMP}`);

// ── Transform JSX via esbuild ──
console.log('   Running esbuild JSX transform...');
let transformed;
try {
    transformed = execSync(
        `npx esbuild "${TEMP}" --jsx=transform --bundle=false --target=es2020`,
        { encoding: 'utf-8', maxBuffer: 1024 * 1024 * 50 }
    );
} catch (e) {
    console.error('❌ esbuild failed:', e.message);
    if (e.stderr) console.error(e.stderr);
    process.exit(1);
}

// Clean up temp file
fs.unlinkSync(TEMP);
console.log('   ✅ JSX transformed successfully');

// ── Post-process: fix const/let → var for top-level declarations ──
const topLevelNames = [
    'RosterKeyPanel', 'SimpleBarChart', 'SimpleDonutChart',
    'LongitudinalProgressChart', 'ConfettiEffect',
    'StudentEscapeRoomOverlay', 'TeacherLiveQuizControls',
    'calculateAnalyticsMetrics', 'LearnerProgressView', 'TeacherDashboard'
];
topLevelNames.forEach(name => {
    const pattern = new RegExp(`const ${name} = `, 'g');
    transformed = transformed.replace(pattern, `var ${name} = `);
});

// ── Icons used by teacher bundle ──
const iconList = [
    'Activity', 'AlertCircle', 'ArrowDown', 'Award', 'BarChart3',
    'BookOpen', 'Calendar', 'CheckCircle', 'CheckCircle2', 'CheckSquare',
    'ChevronDown', 'ClipboardList', 'Clock', 'DoorOpen', 'Download',
    'Eye', 'FileDown', 'Gamepad2', 'History', 'Layers', 'Layout',
    'Lightbulb', 'MapIcon', 'MonitorPlay', 'Play', 'Plus', 'Printer',
    'RefreshCw', 'Search', 'Share2', 'ShieldCheck', 'Sparkles',
    'Target', 'Trash2', 'TrendingUp', 'Trophy', 'Upload', 'Users', 'X'
];

const componentNames = topLevelNames;

// ── Build IIFE wrapper ──
const iifeHeader = `/**
 * AlloFlow Teacher Analytics Module
 *
 * Contains: ${componentNames.join(', ')}
 *
 * Auto-generated by _build_teacher_module.js — DO NOT EDIT MANUALLY.
 * Source: teacher_source.jsx
 */
(function() {
  'use strict';

  // ── Duplicate-load guard ──
  if (window.__teacherModuleLoaded) {
    console.warn('[TeacherModule] Already loaded — skipping');
    return;
  }
  window.__teacherModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[TeacherModule] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useContext = React.useContext;

  // ── App dependencies from window ──
  var LanguageContext = window.AlloLanguageContext;
  var _shared = window.__alloShared || {};
  var safeGetItem = _shared.safeGetItem || function(k) { try { return localStorage.getItem(k); } catch(e) { return null; } };
  var safeSetItem = _shared.safeSetItem || function(k,v) { try { localStorage.setItem(k,v); } catch(e) {} };
  var warnLog = _shared.warnLog || function() { console.warn.apply(console, arguments); };
  var debugLog = window.__alloDebugLog || function() {};

  // ── Shared hooks from host app ──
  var _hooks = window.__alloHooks || {};
  var useFocusTrap = _hooks.useFocusTrap || function() {};

  // ── Firebase dependencies ──
  var _fb = window.__alloFirebase || {};
  var db = _shared.db;
  var doc = _fb.doc || function() { return {}; };
  var updateDoc = _fb.updateDoc || function() { return Promise.resolve(); };

  // ── Lucide icons from host app ──
  var _icons = window.AlloIcons || {};
${iconList.map(ic => `  var ${ic} = _icons.${ic} || function() { return null; };`).join('\n')}

  // ═══════════════════════════════════════════════════════════════
  // TEACHER COMPONENTS (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

`;

const iifeFooter = `

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
${componentNames.map(n => `  window.AlloModules.${n} = ${n};`).join('\n')}

  console.log('[TeacherModule] ${componentNames.length} components registered:', ${JSON.stringify(componentNames)});
})();
`;

const finalModule = iifeHeader + transformed + iifeFooter;

// ── Write output ──
fs.writeFileSync(OUTPUT, finalModule, 'utf-8');
console.log(`\n📄 Written: ${path.relative(__dirname, OUTPUT)} (${finalModule.split('\n').length} lines)`);

fs.writeFileSync(OUTPUT_PUBLIC, finalModule, 'utf-8');
console.log(`📄 Written: ${path.relative(__dirname, OUTPUT_PUBLIC)}`);

console.log(`\n✅ teacher_module.js built successfully with ${componentNames.length} components`);
