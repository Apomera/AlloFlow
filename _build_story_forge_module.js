#!/usr/bin/env node
/**
 * Build story_forge_module.js from story_forge_source.jsx
 *
 * Usage: node _build_story_forge_module.js
 *
 * 1. Reads story_forge_source.jsx
 * 2. Compiles JSX → React.createElement via esbuild (preserves let/arrow per target=es2020)
 * 3. Wraps in IIFE with StoryForge's preamble:
 *    - duplicate-load guard (window.__storyForgeModuleLoaded)
 *    - React hook aliases (useState/useEffect/useRef/useCallback/useMemo/useReducer)
 *    - Lucide icon lazy-shims (resolved from window.AlloIcons)
 * 4. Writes story_forge_module.js + syncs to prismflow-deploy/public/
 *
 * Source file already contains the focus-CSS IIFE and sfAnnounce helper inline; those
 * stay nested inside the outer IIFE in the compiled output. Functionally identical to
 * the previous hand-maintained module.js, with /* @__PURE__ * / annotations from esbuild.
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT = __dirname;
const SOURCE = path.join(ROOT, 'story_forge_source.jsx');
const OUTPUT = path.join(ROOT, 'story_forge_module.js');
const DEPLOY_OUT = path.join(ROOT, 'prismflow-deploy', 'public', 'story_forge_module.js');
const TMP = path.join(ROOT, '_tmp_story_forge_entry.jsx');

if (!fs.existsSync(SOURCE)) {
    console.error('❌ Source not found:', SOURCE);
    process.exit(1);
}

const source = fs.readFileSync(SOURCE, 'utf-8');

// Wrap source in /* global */ comment so esbuild doesn't bark about undefined hooks/icons —
// they're injected by the wrapper preamble.
const entry = `
/* global React, useState, useEffect, useRef, useCallback, useMemo, useReducer */
/* global ArrowLeft, ArrowRight, BookOpen, CheckCircle2, Download, Edit, Eye, HelpCircle, Image, ImageIcon, Mic, Palette, Play, Plus, RefreshCw, Save, Sparkles, Star, Trash2, Type, Volume2, X */

${source}
`;

fs.writeFileSync(TMP, entry, 'utf-8');

console.log('🔨 Compiling story_forge_source.jsx with esbuild...');
try {
    execSync(`npx esbuild "${TMP}" --bundle=false --format=esm --jsx=transform --jsx-factory=React.createElement --jsx-fragment=React.Fragment --outfile="${TMP}.compiled.js" --target=es2020`, {
        cwd: ROOT,
        stdio: 'inherit'
    });
} catch (e) {
    console.error('❌ esbuild compilation failed');
    try { fs.unlinkSync(TMP); } catch(_){}
    process.exit(1);
}

const compiled = fs.readFileSync(TMP + '.compiled.js', 'utf-8')
    .replace(/^\s*\/\*[^*]*global[^*]*\*\/\s*$/gm, '') // strip /* global ... */ pragma lines
    .trim();

try { fs.unlinkSync(TMP); } catch(_){}
try { fs.unlinkSync(TMP + '.compiled.js'); } catch(_){}

const outputCode = `/**
 * AlloFlow StoryForge Module
 *
 * Scaffolded creative writing with AI illustration, narration, grading, and storybook export.
 *
 * Auto-generated by _build_story_forge_module.js — DO NOT EDIT MANUALLY.
 * Source: story_forge_source.jsx
 */
(function() {
  'use strict';

  // ── Duplicate-load guard ──
  if (window.__storyForgeModuleLoaded) {
    console.warn('[StoryForge] Already loaded — skipping');
    return;
  }
  window.__storyForgeModuleLoaded = true;

  // ── React dependencies ──
  var React = window.React;
  if (!React) { console.error('[StoryForge] React not found on window'); return; }
  var useState = React.useState;
  var useEffect = React.useEffect;
  var useRef = React.useRef;
  var useCallback = React.useCallback;
  var useMemo = React.useMemo;
  var useReducer = React.useReducer;

  // ── Lucide icons from host app (lazy-resolved at module load) ──
  var _icons = window.AlloIcons || {};
  var ArrowLeft = _icons.ArrowLeft || function() { return null; };
  var ArrowRight = _icons.ArrowRight || function() { return null; };
  var BookOpen = _icons.BookOpen || function() { return null; };
  var CheckCircle2 = _icons.CheckCircle2 || function() { return null; };
  var Download = _icons.Download || function() { return null; };
  var Edit = _icons.Edit || function() { return null; };
  var Eye = _icons.Eye || function() { return null; };
  var HelpCircle = _icons.HelpCircle || function() { return null; };
  var Image = _icons.Image || function() { return null; };
  var ImageIcon = _icons.ImageIcon || function() { return null; };
  var Mic = _icons.Mic || function() { return null; };
  var Palette = _icons.Palette || function() { return null; };
  var Play = _icons.Play || function() { return null; };
  var Plus = _icons.Plus || function() { return null; };
  var RefreshCw = _icons.RefreshCw || function() { return null; };
  var Save = _icons.Save || function() { return null; };
  var Sparkles = _icons.Sparkles || function() { return null; };
  var Star = _icons.Star || function() { return null; };
  var Trash2 = _icons.Trash2 || function() { return null; };
  var Type = _icons.Type || function() { return null; };
  var Volume2 = _icons.Volume2 || function() { return null; };
  var X = _icons.X || function() { return null; };

  // ═══════════════════════════════════════════════════════════════
  // STORYFORGE COMPONENT (JSX pre-transformed by esbuild)
  // ═══════════════════════════════════════════════════════════════

${compiled}

  // ═══════════════════════════════════════════════════════════════
  // REGISTRATION
  // ═══════════════════════════════════════════════════════════════
  window.AlloModules = window.AlloModules || {};
  window.AlloModules.StoryForge = StoryForge;

  console.log('[StoryForge] Module registered');
})();
`;

fs.writeFileSync(OUTPUT, outputCode, 'utf-8');
fs.writeFileSync(DEPLOY_OUT, outputCode, 'utf-8');
const lineCount = outputCode.split('\n').length;
console.log(`✅ Built ${OUTPUT} (${lineCount} lines)`);
console.log(`✅ Synced to ${DEPLOY_OUT}`);
