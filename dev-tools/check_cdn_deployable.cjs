#!/usr/bin/env node
/*
 * check_cdn_deployable.cjs — gate for the two failure classes that froze
 * alloflow-cdn.pages.dev for 3 days (2026-07-03 → 2026-07-05 post-mortem):
 *
 * (1) ASSET SIZE — Cloudflare hard-fails ANY deploy (classic Pages or Workers
 *     assets) containing a file over 25 MiB. The 30.7 MiB ffmpeg-core.wasm,
 *     swept into git by deploy.sh Step 6's `git add desktop/web-app/public/`,
 *     killed every CDN build for days. Classic Pages does NOT honor
 *     .assetsignore, so a tracked oversized file ANYWHERE in the repo is fatal
 *     even when "excluded". We therefore check what a clone would contain:
 *     the HEAD tree (fast, no filesystem) plus any files staged right now
 *     (they are about to be committed by Step 1).
 *     HARD FAIL at ≥ 25 MiB. Warn at ≥ 20 MiB (early-warning band — e.g.
 *     word_audio_bank_part3.json sits at 20.9 MiB today).
 *
 * (2) NPM-10 LOCK COMPAT — Cloudflare's build image runs npm 10.x, whose
 *     `npm ci` is stricter than npm 11's. A package-lock.json regenerated by
 *     npm 11 can pass every local check and still hard-fail the cloud build
 *     ("npm error Missing: @emnapi/core@... from lock file" / EUSAGE). We
 *     validate with the real npm 10 via npx. If npx itself flakes (Windows
 *     npm-cache file-locking has wedged it on this machine before), that is
 *     an ADVISORY, not a block — only a genuine lock-sync failure blocks.
 *     Fix command when this gate trips:
 *       npx -y npm@10.9.2 install --package-lock-only --ignore-scripts
 *
 * Usage: node dev-tools/check_cdn_deployable.cjs [--quiet] [--skip-npm10]
 * Exit: non-zero on a blocking failure. set -e in deploy.sh aborts the deploy.
 */
'use strict';
const { execSync, spawnSync } = require('child_process');
const fs = require('fs');

const QUIET = process.argv.includes('--quiet');
const SKIP_NPM10 = process.argv.includes('--skip-npm10');
const HARD = 25 * 1024 * 1024;   // Cloudflare per-file limit
const WARN = 20 * 1024 * 1024;   // early-warning band
const CLOUD_NPM = '10.9.2';      // npm in Cloudflare's build image (from build logs)

let failed = false;
const log = (m) => { if (!QUIET) console.log(m); };

// ── (1) asset size: HEAD tree + currently-staged files ──────────────────
const offenders = [];
const warnings = [];
try {
  const tree = execSync('git ls-tree -r -l HEAD', { encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 });
  for (const line of tree.split('\n')) {
    const m = line.match(/^\S+\s+blob\s+\S+\s+(\d+)\t(.+)$/);
    if (!m) continue;
    const size = Number(m[1]);
    if (size >= HARD) offenders.push({ path: m[2], size, where: 'HEAD' });
    else if (size >= WARN) warnings.push({ path: m[2], size, where: 'HEAD' });
  }
  // Staged-but-uncommitted additions/modifications (Step 1 commits these next).
  const staged = execSync('git diff --cached --name-only --diff-filter=AM', { encoding: 'utf8' })
    .split('\n').map(s => s.trim()).filter(Boolean);
  for (const p of staged) {
    let size = 0;
    try { size = fs.statSync(p).size; } catch (_) { continue; }
    if (size >= HARD) offenders.push({ path: p, size, where: 'staged' });
    else if (size >= WARN) warnings.push({ path: p, size, where: 'staged' });
  }
} catch (e) {
  console.error('✗ check_cdn_deployable: git scan failed: ' + e.message);
  process.exit(1);
}
const mib = (n) => (n / 1048576).toFixed(1) + ' MiB';
if (offenders.length) {
  failed = true;
  console.error('✗ ' + offenders.length + ' tracked/staged file(s) at or over Cloudflare\'s 25 MiB per-file limit — the CDN deploy WILL fail (the ffmpeg-core.wasm class):');
  offenders.forEach(o => console.error('   - ' + mib(o.size) + '  ' + o.path + '  [' + o.where + ']'));
  console.error('  Untrack it (git rm --cached <path>) and add a scoped .gitignore; keep the file on disk if Firebase/desktop need it.');
}
warnings.forEach(w => console.error('  ⚠ ' + mib(w.size) + '  ' + w.path + ' — within 5 MiB of the 25 MiB Cloudflare limit (advisory).'));
if (!offenders.length) log('  ✓ no tracked/staged file ≥ 25 MiB (largest-band warnings: ' + warnings.length + ').');

// ── (2) npm-10 lock compatibility ────────────────────────────────────────
if (!SKIP_NPM10) {
  const r = spawnSync('npx', ['-y', 'npm@' + CLOUD_NPM, 'ci', '--dry-run', '--ignore-scripts'], {
    encoding: 'utf8', shell: process.platform === 'win32', timeout: 240000, maxBuffer: 16 * 1024 * 1024,
  });
  const out = (r.stdout || '') + '\n' + (r.stderr || '');
  const lockBroken = /Missing:.*from lock file|code EUSAGE/.test(out);
  if (r.status === 0) {
    log('  ✓ root package-lock.json validates under npm ' + CLOUD_NPM + ' (Cloudflare\'s npm ci).');
  } else if (lockBroken) {
    failed = true;
    console.error('✗ npm ' + CLOUD_NPM + ' rejects the root lock (the @emnapi/npm-11 class) — the CDN build WILL fail at npm ci:');
    (out.match(/Missing:[^\n]*/g) || []).slice(0, 6).forEach(l => console.error('   - ' + l.trim()));
    console.error('  Fix: npx -y npm@' + CLOUD_NPM + ' install --package-lock-only --ignore-scripts   (then commit package-lock.json)');
  } else {
    // npx infrastructure flake (cache wedge, network) — advisory only, never block on tooling noise.
    console.error('  ⚠ npm-10 lock check could not run (npx exit ' + r.status + ' without a lock-sync signature) — advisory only.');
  }
}

if (failed) process.exit(1);
log('✓ check_cdn_deployable: repo is CDN-deployable (asset sizes + npm-10 lock).');
